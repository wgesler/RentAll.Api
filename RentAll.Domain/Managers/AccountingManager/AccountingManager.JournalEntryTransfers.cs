    #region Journal Entry
    private async Task TryReplaceJournalEntriesFromTransferAsync(Transfer transfer, Guid currentUser)
    {
        try
        {
            if (transfer.TransferId == Guid.Empty)
                return;

            if (!await IsAccountingFeatureEnabledAsync(transfer.OrganizationId) || !ShouldCreateJournalEntryForTransfer(transfer))
            {
                await DeleteJournalEntriesForSourceAsync(
                    transfer.OrganizationId,
                    transfer.OfficeId,
                    (int)SourceType.Transfer,
                    transfer.TransferId);

                if (transfer.JournalEntryId.HasValue && transfer.JournalEntryId != Guid.Empty)
                {
                    transfer.JournalEntryId = null;
                    transfer.ModifiedBy = currentUser;
                    await _accountingRepository.UpdateTransferAsync(transfer);
                }

                return;
            }

            var existingEntries = (await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
            {
                OrganizationId = transfer.OrganizationId,
                OfficeIds = transfer.OfficeId.ToString(),
                SourceTypeId = (int)SourceType.Transfer,
                SourceId = transfer.TransferId,
                IncludeVoided = true,
                IncludeUnposted = true
            })).ToList();

            var rebuiltJournalEntry = await BuildJournalEntryFromTransferRecordAsync(transfer, currentUser);

            while (existingEntries.Count > 1)
            {
                var duplicate = existingEntries[^1];
                await DeleteJournalEntryAsync(duplicate.JournalEntryId, transfer.OrganizationId);
                existingEntries.RemoveAt(existingEntries.Count - 1);
            }

            var existingEntry = existingEntries.FirstOrDefault();
            if (existingEntry != null)
            {
                rebuiltJournalEntry.JournalEntryId = existingEntry.JournalEntryId;
                rebuiltJournalEntry.OrganizationId = transfer.OrganizationId;
                rebuiltJournalEntry.OfficeId = transfer.OfficeId;
                rebuiltJournalEntry.CreatedBy = existingEntry.CreatedBy;
                rebuiltJournalEntry.ModifiedBy = currentUser;

                foreach (var line in rebuiltJournalEntry.JournalEntryLines)
                {
                    line.JournalEntryId = existingEntry.JournalEntryId;
                    line.JournalEntryLineId = Guid.Empty;
                }

                await UpdateAutoGeneratedJournalEntryAsync(rebuiltJournalEntry);

                if (transfer.JournalEntryId != existingEntry.JournalEntryId)
                {
                    transfer.JournalEntryId = existingEntry.JournalEntryId;
                    transfer.ModifiedBy = currentUser;
                    await _accountingRepository.UpdateTransferAsync(transfer);
                }

                return;
            }

            var createdJournalEntry = await CreateAutoGeneratedJournalEntryAsync(rebuiltJournalEntry);
            if (createdJournalEntry == null)
                return;

            if (transfer.JournalEntryId != createdJournalEntry.JournalEntryId)
            {
                transfer.JournalEntryId = createdJournalEntry.JournalEntryId;
                transfer.ModifiedBy = currentUser;
                await _accountingRepository.UpdateTransferAsync(transfer);
            }
        }
        catch (Exception ex)
        {
            var transferLabel = string.IsNullOrWhiteSpace(transfer.TransferCode)
                ? transfer.TransferId.ToString()
                : transfer.TransferCode.Trim();

            await LogAccountingErrorAsync(
                trigger: "Transfer",
                organizationId: transfer.OrganizationId,
                officeId: transfer.OfficeId,
                sourceTypeId: (int)SourceType.Transfer,
                sourceId: transfer.TransferId,
                documentCode: transferLabel,
                accountingPeriod: transfer.AccountingPeriod == default ? null : transfer.AccountingPeriod,
                amount: transfer.Amount,
                message: $"Transfer {transferLabel} journal entry refresh failed: {ex.Message}",
                currentUser: currentUser);
        }
    }

    private static void ValidateTransferForJournalEntry(Transfer transfer)
    {
        if (transfer.OfficeId <= 0)
            throw new Exception("OfficeId is required");

        if (transfer.Amount == 0)
            throw new Exception("Transfer amount cannot be zero");

        if (transfer.TransferDate == default)
            throw new Exception("Transfer date is required");

        if (transfer.Splits == null || transfer.Splits.Count == 0)
            throw new Exception("At least one split is required");

        foreach (var split in transfer.Splits)
        {
            if (split.ChartOfAccountId is null or <= 0)
                throw new Exception("Each split must have a chart of account");

            if (split.Amount == 0)
                throw new Exception("Split amounts must be non-zero");
        }

        var splitTotal = transfer.Splits.Sum(split => split.Amount);
        if (Math.Abs(splitTotal - transfer.Amount) > 0.005m)
            throw new Exception($"Transfer amount must equal split total ({splitTotal:0.00})");
    }

    private static bool ShouldCreateJournalEntryForTransfer(Transfer transfer)
        => transfer.IsActive
            && transfer.OfficeId > 0
            && transfer.Amount != 0
            && transfer.TransferDate != default
            && transfer.Splits is { Count: > 0 }
            && transfer.Splits.All(split => split.ChartOfAccountId is > 0 && split.Amount != 0)
            && Math.Abs(transfer.Splits.Sum(split => split.Amount) - transfer.Amount) <= 0.005m;

    private async Task<JournalEntry> BuildJournalEntryFromTransferRecordAsync(Transfer transfer, Guid currentUser)
    {
        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(transfer.OrganizationId, transfer.OfficeId);
        return BuildJournalEntryFromTransferRecord(transfer, chartOfAccounts, accountingOffice, currentUser);
    }

    private JournalEntry BuildJournalEntryFromTransferRecord(Transfer transfer, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // TRANSFER-JE-ACCOUNTS
        // Lump-sum posting only (bank reconcile sees one bank line; split detail lives on TransferSplit):
        // Line 1 — Escrow Deposits (GetDefaultEscrowDepositAccount):
        //   positive transfer: Credit escrow / Debit 0
        //   negative transfer: Debit escrow / Credit 0
        // Line 2 — Bank account (transfer.BankAccountId or default bank):
        //   positive transfer: Debit bank / Credit 0
        //   negative transfer: Debit 0 / Credit bank
        // END TRANSFER-JE-ACCOUNTS

        var accounts = ResolveTransferJournalEntryAccounts(chartOfAccounts, transfer.OfficeId, accountingOffice);
        var escrowDepositAccountId = accounts.EscrowDepositAccountId;
        var bankAccountId = transfer.BankAccountId is > 0
            ? transfer.BankAccountId.Value
            : accounts.BankAccountId;
        var memo = BuildTransferMemo(transfer.TransferCode, transfer.TransferDate);
        var headerLineContext = FirstTransferSplitContext(transfer.Splits) with
        {
            PropertyId = transfer.PropertyId ?? FirstSplitContextId(transfer.Splits, split => split.PropertyId)
        };
        var escrowLine = new JournalEntryLine
        {
            ChartOfAccountId = escrowDepositAccountId,
            Debit = transfer.Amount < 0 ? Math.Abs(transfer.Amount) : 0,
            Credit = transfer.Amount > 0 ? transfer.Amount : 0,
            Memo = memo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(escrowLine, headerLineContext);

        var bankLine = new JournalEntryLine
        {
            ChartOfAccountId = bankAccountId,
            Debit = transfer.Amount > 0 ? transfer.Amount : 0,
            Credit = transfer.Amount < 0 ? Math.Abs(transfer.Amount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(bankLine, headerLineContext);

        return new JournalEntry
        {
            OrganizationId = transfer.OrganizationId,
            OfficeId = transfer.OfficeId,
            TransactionDate = transfer.TransferDate,
            PostingDate = transfer.AccountingPeriod,
            SourceTypeId = (int)SourceType.Transfer,
            SourceId = transfer.TransferId,
            SourceCode = ResolveJournalEntrySourceCodeFromTransfer(transfer),
            Memo = memo,
            JournalEntryLines = [escrowLine, bankLine],
            CreatedBy = currentUser
        };
    }

    private TransferJournalEntryAccounts ResolveTransferJournalEntryAccounts(List<ChartOfAccount> chartOfAccounts, int officeId, AccountingOffice? accountingOffice)
    {
        return new TransferJournalEntryAccounts
        {
            EscrowDepositAccountId = GetDefaultEscrowDepositAccount(chartOfAccounts, officeId, accountingOffice),
            EscrowSecurityDepositAccountId = GetDefaultEscrowSecurityDepositAccount(chartOfAccounts, officeId, accountingOffice),
            EscrowSdwAccountId = GetDefaultEscrowSdwAccount(chartOfAccounts, officeId, accountingOffice),
            EscrowOwnersAccountId = GetDefaultEscrowOwnersAccount(chartOfAccounts, officeId, accountingOffice),
            BankAccountId = GetDefaultBankAccount(chartOfAccounts, officeId, accountingOffice)
        };
    }

    private static bool IsAllowedTransferDebitAccountId(int chartOfAccountId, TransferJournalEntryAccounts accounts)
        => chartOfAccountId == accounts.EscrowSecurityDepositAccountId
            || chartOfAccountId == accounts.EscrowSdwAccountId
            || chartOfAccountId == accounts.EscrowOwnersAccountId
            || chartOfAccountId == accounts.BankAccountId;
