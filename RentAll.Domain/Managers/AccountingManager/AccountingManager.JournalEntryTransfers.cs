    #region Triggers
    public async Task<JournalEntry?> CreateJournalEntryFromTransferAsync(Transfer transfer, Guid currentUser)
        => (await CreateJournalEntryFromTransferWithResultAsync(transfer, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromTransferWithResultAsync(Transfer transfer, Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(transfer.OrganizationId))
            return AccountingJournalEntryResult.Success();

        if (!ShouldCreateJournalEntryForTransfer(transfer))
            return AccountingJournalEntryResult.Success();

        try
        {
            ValidateTransferForJournalEntry(transfer);

            await EnrichTransferSplitsFromJournalEntryLinesAsync(transfer);
            ApplyTransferHeaderContextFromSplits(transfer);

            var journalEntry = await BuildJournalEntryFromTransferRecordAsync(transfer, currentUser);
            var created = await CreateAutoGeneratedJournalEntryAsync(journalEntry);
            if (created == null)
                return AccountingJournalEntryResult.Success();

            if (transfer.JournalEntryId != created.JournalEntryId)
            {
                transfer.JournalEntryId = created.JournalEntryId;
                transfer.ModifiedBy = currentUser;
                await _accountingRepository.UpdateTransferAsync(transfer);
            }

            return AccountingJournalEntryResult.Success(created);
        }
        catch (Exception ex)
        {
            var transferLabel = string.IsNullOrWhiteSpace(transfer.TransferCode)
                ? transfer.TransferId.ToString()
                : transfer.TransferCode.Trim();

            await LogAccountingErrorAsync(
                trigger: "Transfer",
                organizationId: transfer.OrganizationId,
                officeId: transfer.OfficeId,
                sourceTypeId: (int)SourceType.Transfer,
                sourceId: transfer.TransferId,
                documentCode: transferLabel,
                accountingPeriod: transfer.AccountingPeriod == default ? null : transfer.AccountingPeriod,
                amount: transfer.Amount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    public async Task<Transfer> UpdateTransferAsync(Transfer transfer, Guid currentUser)
    {
        var existing = await _accountingRepository.GetTransferByIdAsync(transfer.TransferId, transfer.OrganizationId)
            ?? throw new Exception("Transfer not found");

        transfer.CreatedBy = existing.CreatedBy;
        transfer.JournalEntryId = existing.JournalEntryId;
        transfer.TransferCode = existing.TransferCode;

        await PrepareTransferForSaveAsync(transfer);
        await _accountingRepository.UpdateTransferAsync(transfer);

        var freshTransfer = await _accountingRepository.GetTransferByIdAsync(transfer.TransferId, transfer.OrganizationId)
            ?? throw new Exception("Transfer not found after update");

        await TryReplaceJournalEntriesFromTransferAsync(freshTransfer, currentUser);

        return await _accountingRepository.GetTransferByIdAsync(freshTransfer.TransferId, freshTransfer.OrganizationId)
            ?? freshTransfer;
    }
