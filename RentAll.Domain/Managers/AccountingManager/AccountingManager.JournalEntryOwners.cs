    private async Task<JournalEntry?> CreateJournalEntryFromInvoiceForOwnerShareAsync(Invoice invoice, decimal rentPlus4000Base, Guid currentUser)
        => await UpsertJournalEntryFromInvoiceForOwnerShareAsync(invoice, rentPlus4000Base, [], currentUser);

    private async Task<JournalEntry?> UpsertJournalEntryFromInvoiceForOwnerShareAsync(
        Invoice invoice,
        decimal rentPlus4000Base,
        IReadOnlyList<JournalEntry> existingInvoiceEntries,
        Guid currentUser)
    {
        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (propertyId == null || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        var propertyAgreement = await _propertyRepository.GetPropertyAgreementByPropertyIdAsync(propertyId.Value);
        if (property == null || propertyAgreement == null)
            return null;

        var accountContextTask = LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var officeCostCodeContextTask = LoadOfficeCostCodeContextAsync(invoice.OrganizationId, invoice.OfficeId);
        await Task.WhenAll(accountContextTask, officeCostCodeContextTask);
        var (chartOfAccounts, accountingOffice) = await accountContextTask;
        var (office, costCodeById) = await officeCostCodeContextTask;

        var ownerAmount = await CalculateOwnerShareAmountForInvoiceAsync(invoice, rentPlus4000Base);
        if (!ownerAmount.HasValue || ownerAmount.Value == 0)
        {
            var existingOwnerShare = existingInvoiceEntries.FirstOrDefault(entry =>
                IsInvoiceOwnerShareJournalEntry(entry)
                && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));
            if (existingOwnerShare != null)
                await DeleteOpenJournalEntryAsync(existingOwnerShare.JournalEntryId, invoice.OrganizationId);
            return null;
        }

        List<JournalEntryLine>? journalEntryLines = new List<JournalEntryLine>();
        if (property.PropertyLeaseType == PropertyLeaseType.PropertyManagement)
            journalEntryLines = BuildJournalEntryForOwnerShare(invoice, ownerAmount.Value, property, chartOfAccounts, accountingOffice, office, costCodeById, propertyId.Value, currentUser);

        if (journalEntryLines == null || journalEntryLines.Count == 0)
        {
            var existingOwnerShare = existingInvoiceEntries.FirstOrDefault(entry =>
                IsInvoiceOwnerShareJournalEntry(entry)
                && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));
            if (existingOwnerShare != null)
                await DeleteOpenJournalEntryAsync(existingOwnerShare.JournalEntryId, invoice.OrganizationId);
            return null;
        }

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner share journal entry for an invoice");

        var ownerJournalEntry = ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            SourceCode = ResolveJournalEntrySourceCodeFromInvoice(invoice),
            Memo = BuildOwnerExpectedRentMemo(invoice),
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        }, JournalEntryKind.OwnerExpected, Perspective.Owner);

        var ownerShareExisting = existingInvoiceEntries.FirstOrDefault(entry =>
            IsInvoiceOwnerShareJournalEntry(entry)
            && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));
        return await UpsertAutoGeneratedJournalEntryAsync(
            ownerJournalEntry,
            ownerShareExisting != null ? [ownerShareExisting] : [],
            currentUser,
            invoice.OrganizationId);
    }

