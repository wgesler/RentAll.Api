    private List<JournalEntryLine> BuildJournalEntryForOwnerShare(Invoice invoice, decimal ownerAmount, Property property, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Office? office, IReadOnlyDictionary<int, CostCode> costCodeById, Guid propertyId, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // OWNER-SHARE-JE-ACCOUNTS
        //   Line 1 — Debit: Tenant rental expense for the invoice rental ledger line.
        //   Line 2 — Credit: Default Own.
        // END OWNER-SHARE-JE-ACCOUNTS

        var journalEntryLines = new List<JournalEntryLine>();

        if (!TryGetInvoiceRentalLedgerLine(invoice, out var rentalLine))
            return journalEntryLines;

        var isFurnished = !property.Unfurnished;
        var memo = BuildOwnerExpectedRentMemo(invoice);
        var ownerContactId = property.Owner1Id;
        var lineContext = CreateJournalEntryLineContextFromInvoice(invoice, propertyId) with
        {
            ContactId = NormalizeOptionalGuid(ownerContactId)
        };
        costCodeById.TryGetValue(rentalLine.CostCodeId, out var rentalCostCode);
        var ownerAccountsPayableId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var ownerRentExpenseCostCodeId = isFurnished ? office?.FurnishedRentExpenseCcId : office?.UnfurnishedRentExpenseCcId;
        var ownerRentExpenseAccountId = isFurnished
            ? GetDefaultFurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice)
            : GetDefaultUnfurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice);

        var ownerExpenseLine = new JournalEntryLine
        {
            ChartOfAccountId = ownerRentExpenseAccountId,
            CostCodeId = rentalLine.CostCodeId,
            Debit = ownerAmount > 0 ? ownerAmount : 0,
            Credit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(ownerExpenseLine, lineContext);
        journalEntryLines.Add(ownerExpenseLine);

        var ownerPayableLine = new JournalEntryLine
        {
            ChartOfAccountId = ownerAccountsPayableId,
            CostCodeId = ownerRentExpenseCostCodeId is > 0 ? ownerRentExpenseCostCodeId : null,
            Debit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Credit = ownerAmount > 0 ? ownerAmount : 0,
            Memo = memo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(ownerPayableLine, lineContext);
        journalEntryLines.Add(ownerPayableLine);

        return journalEntryLines;
    }

    private async Task TryCreateOwnerActualJournalEntryForPaymentAsync(Invoice invoice, LedgerLine paymentLedgerLine, decimal paymentAmount, DateOnly transactionDate, Guid currentUser)
    {
        if (paymentAmount == 0 || paymentLedgerLine.LedgerLineId == Guid.Empty)
            return;

        try
        {
            IReadOnlyList<JournalEntry>? workingEntries = null;
            if (InvoiceCrossesAccountingPeriodBoundary(invoice))
            {
                workingEntries = await GetAllJournalEntriesForInvoiceAsync(
                    invoice.OrganizationId,
                    invoice.OfficeId,
                    invoice.InvoiceId);
            }

            var journalEntry = await BuildJournalEntryFromPaymentForOwnerActualAsync(
                invoice,
                paymentLedgerLine,
                paymentAmount,
                transactionDate,
                currentUser,
                workingEntries);
            if (journalEntry == null || !journalEntry.JournalEntryLines.Any(line => line.Debit != 0 || line.Credit != 0))
                return;

            await CreateAutoGeneratedJournalEntryAsync(journalEntry);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "InvoiceOwnerActual",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.InvoicePayment,
                sourceId: paymentLedgerLine.LedgerLineId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: paymentAmount,
                message: ex.Message,
                currentUser: currentUser);
        }
    }

    private async Task<JournalEntry?> BuildJournalEntryFromPaymentForOwnerActualAsync(
        Invoice invoice,
        LedgerLine paymentLedgerLine,
        decimal paymentAmount,
        DateOnly transactionDate,
        Guid currentUser,
        IReadOnlyList<JournalEntry>? workingEntries = null)
    {
        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (propertyId == null || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        var propertyAgreement = await _propertyRepository.GetPropertyAgreementByPropertyIdAsync(propertyId.Value);
        if (property == null || propertyAgreement == null || property.PropertyLeaseType != PropertyLeaseType.PropertyManagement)
            return null;

        // Cross-period slices: OwnAct = Owner Expected (OwnRent) for that period when the slice is collected.
        var actualAmount = InvoiceCrossesAccountingPeriodBoundary(invoice)
            ? await ResolveOwnerActualAmountForSliceAsync(invoice, paymentAmount, workingEntries)
            : await CalculateOwnerActualAmountForInvoicePaymentAsync(invoice, paymentAmount);
        if (!actualAmount.HasValue || actualAmount.Value == 0)
            return null;

        var accountContextTask = LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var officeCostCodeContextTask = LoadOfficeCostCodeContextAsync(invoice.OrganizationId, invoice.OfficeId);
        await Task.WhenAll(accountContextTask, officeCostCodeContextTask);
        var (chartOfAccounts, accountingOffice) = await accountContextTask;
        var (office, costCodeById) = await officeCostCodeContextTask;
        var journalEntryLines = BuildJournalEntryLinesForOwnerActual(
            invoice,
            actualAmount.Value,
            property,
            chartOfAccounts,
            accountingOffice,
            office,
            costCodeById,
            propertyId.Value,
            currentUser);
        if (journalEntryLines.Count == 0)
            return null;

        return ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = transactionDate,
            AccountingPeriod = invoice.AccountingPeriod != default
                ? invoice.AccountingPeriod
                : FirstDayOfMonth(transactionDate),
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            SourceCode = ResolveJournalEntrySourceCodeFromInvoice(invoice),
            Memo = BuildOwnerActualRentMemo(invoice),
            IsCashOnly = true,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        }, JournalEntryKind.OwnerActual, Perspective.Owner);
    }

