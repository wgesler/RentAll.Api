    #region Trigger Invoice Owner Share
    private async Task<JournalEntry?> CreateJournalEntryFromInvoiceForOwnerShareAsync(Invoice invoice, decimal amount, Guid currentUser)
    {
        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (propertyId == null || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        var propertyAgreement = await _propertyRepository.GetPropertyAgreementByPropertyIdAsync(propertyId.Value);
        if (property == null || propertyAgreement == null)
            return null;

        var accountContextTask = LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var officeCostCodeContextTask = LoadOfficeCostCodeContextAsync(invoice.OrganizationId, invoice.OfficeId);
        await Task.WhenAll(accountContextTask, officeCostCodeContextTask);
        var (chartOfAccounts, accountingOffice) = await accountContextTask;
        var (office, costCodeById) = await officeCostCodeContextTask;

        var ownerAmount = await CalculateOwnerShareAmountForInvoiceAsync(invoice, amount);
        if (!ownerAmount.HasValue || ownerAmount.Value == 0)
            return null;

        List<JournalEntryLine>? journalEntryLines = new List<JournalEntryLine>();
        if (property.PropertyLeaseType == PropertyLeaseType.PropertyManagement)
            journalEntryLines = BuildJournalEntryForOwnerShare(invoice, ownerAmount.Value, property, chartOfAccounts, accountingOffice, office, costCodeById, propertyId.Value, currentUser);

        if (journalEntryLines == null || journalEntryLines.Count == 0)
            return null;

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner share journal entry for an invoice");

        var ownerJournalEntry = new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            Memo = BuildOwnerShareInvoiceMemo(invoice),
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };

        return await CreateAutoGeneratedJournalEntryAsync(ownerJournalEntry);
    }

    private async Task<AccountingJournalEntryResult> CreateOwnerSharePaymentJournalEntryWithResultAsync(Invoice invoice, LedgerLine paymentLedgerLine, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser, JournalEntry? fallbackJournalEntry = null)
    {
        try
        {
            var ownerPaymentJournalEntry = await BuildJournalEntryForOwnerSharePaymentAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
            if (ownerPaymentJournalEntry != null)
                await CreateAutoGeneratedJournalEntryAsync(ownerPaymentJournalEntry);
            return AccountingJournalEntryResult.Success(fallbackJournalEntry);
        }
        catch (Exception ex)
        {
            var ownerPaymentMessage = $"Owner payment journal entry failed for invoice {invoice.InvoiceCode}: {ex.Message}";
            await LogAccountingErrorAsync(
                trigger: "InvoicePaymentOwnerShare",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.InvoicePayment,
                sourceId: paymentLedgerLine.LedgerLineId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: paymentLedgerLine.Amount,
                message: ownerPaymentMessage,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ownerPaymentMessage, fallbackJournalEntry);
        }
    }
