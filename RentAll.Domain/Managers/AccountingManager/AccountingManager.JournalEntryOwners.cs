    private List<JournalEntryLine> BuildJournalEntryLinesForPropertyManagement(Invoice invoice, decimal ownerAmount, Property property, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Office? office, IReadOnlyDictionary<int, CostCode> costCodeById, Guid propertyId, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // OWNER-SHARE-JE-ACCOUNTS
        // Line 1 — Debit: Tenant rental expense for the invoice rental ledger line.
        // Line 2 — Credit: Default Own.
        // END OWNER-SHARE-JE-ACCOUNTS

        var journalEntryLines = new List<JournalEntryLine>();

        if (!TryGetInvoiceRentalLedgerLine(invoice, out var rentalLine))
            return journalEntryLines;

        var isFurnished = !property.Unfurnished;
        var memo = BuildOwnerShareInvoiceMemo(invoice);
        var ownerContactId = property.Owner1Id;
        costCodeById.TryGetValue(rentalLine.CostCodeId, out var rentalCostCode);
        var ownerAccountsPayableId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var ownerRentExpenseCostCodeId = isFurnished ? office?.FurnishedRentExpenseCcId : office?.UnfurnishedRentExpenseCcId;
        var ownerRentExpenseAccountId = isFurnished
            ? GetDefaultFurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice)
            : GetDefaultUnfurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice);

        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = ownerRentExpenseAccountId,
            CostCodeId = rentalLine.CostCodeId,
            PropertyId = propertyId,
            ReservationId = invoice.ReservationId,
            ContactId = ownerContactId,
            Debit = ownerAmount > 0 ? ownerAmount : 0,
            Credit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        });

        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = ownerAccountsPayableId,
            CostCodeId = ownerRentExpenseCostCodeId is > 0 ? ownerRentExpenseCostCodeId : null,
            PropertyId = propertyId,
            ReservationId = invoice.ReservationId,
            ContactId = ownerContactId,
            Debit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Credit = ownerAmount > 0 ? ownerAmount : 0,
            Memo = memo,
            CreatedBy = currentUser
        });

        return journalEntryLines;
    }

    private async Task<JournalEntry?> BuildOwnerUtilityJournalEntryFromBillAsync(Receipt bill, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // BILL-OWNER-UTILITY-JE-ACCOUNTS
        // Trigger condition: Bill IsUtility = true and the bill split set is Owner type.
        // Line 1 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable) for owner utility total.
        // Line 2 — Credit: PM Utility Income (GetDefaultPmUtilityIncome) for owner utility total.
        // Negative owner utility totals (bill credits) reverse debit/credit direction.
        // END BILL-OWNER-UTILITY-JE-ACCOUNTS

        EnsureReceiptIsBill(bill);
        bill = await LoadReceiptWithSplitsAsync(bill);

        if (!ShouldCreateOwnerUtilityJournalEntryForBill(bill))
            return null;

        if (bill.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner utility journal entry for a bill");

        var ownerSplitLines = ResolveReceiptSplitLines(bill)
            .Where(split => split.ReceiptType == ReceiptType.Owner)
            .ToList();
        var ownerUtilityAmount = ownerSplitLines.Sum(split => split.Amount);

        if (ownerUtilityAmount == 0)
            return null;

        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, bill.OfficeId, accountingOffice);
        var pmUtilityIncomeAccountId = GetDefaultPmUtilityIncome(chartOfAccounts, bill.OfficeId, accountingOffice);
        var billLabel = !string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.BillNumber.Trim() : bill.ReceiptCode.Trim();
        var receiptCode = bill.ReceiptCode.Trim();
        var propertyId = ownerSplitLines
            .Select(split => split.PropertyId)
            .FirstOrDefault(splitPropertyId => splitPropertyId is { } id && id != Guid.Empty)
            ?? bill.PropertyIds.FirstOrDefault(id => id != Guid.Empty);
        Guid? ownerContactId = null;
        if (propertyId != Guid.Empty)
        {
            var property = await _propertyRepository.GetPropertyByIdAsync(propertyId, bill.OrganizationId);
            if (property?.Owner1Id is { } resolvedOwnerId && resolvedOwnerId != Guid.Empty)
                ownerContactId = resolvedOwnerId;
        }
        var memo = BuildOwnerUtilityReceiptMemo(receiptCode, ownerSplitLines);

        var ownerAccountsPayableLine = new JournalEntryLine
        {
            ChartOfAccountId = ownerAccountsPayableAccountId,
            PropertyId = propertyId == Guid.Empty ? null : propertyId,
            ContactId = ownerContactId,
            Debit = ownerUtilityAmount > 0 ? ownerUtilityAmount : 0,
            Credit = ownerUtilityAmount < 0 ? Math.Abs(ownerUtilityAmount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        };

        var pmUtilityIncomeLine = new JournalEntryLine
        {
            ChartOfAccountId = pmUtilityIncomeAccountId,
            PropertyId = propertyId == Guid.Empty ? null : propertyId,
            ContactId = ownerContactId,
            Debit = ownerUtilityAmount < 0 ? Math.Abs(ownerUtilityAmount) : 0,
            Credit = ownerUtilityAmount > 0 ? ownerUtilityAmount : 0,
            Memo = memo,
            CreatedBy = currentUser
        };

        return new JournalEntry
        {
            OrganizationId = bill.OrganizationId,
            OfficeId = bill.OfficeId,
            TransactionDate = bill.AccountingPeriod,
            PostingDate = bill.AccountingPeriod,
            SourceTypeId = (int)SourceType.Bill,
            SourceId = bill.ReceiptId,
            Memo = memo,
            JournalEntryLines = new List<JournalEntryLine> { ownerAccountsPayableLine, pmUtilityIncomeLine },
            CreatedBy = currentUser
        };
    }

    private static string BuildOwnerShareInvoiceMemo(Invoice invoice)
    {
        var invoiceDescription = (invoice.Notes ?? string.Empty).Trim();
        return string.IsNullOrWhiteSpace(invoiceDescription)
            ? $"Owner: Rent: {invoice.InvoiceCode}"
            : $"Owner: Rent: {invoice.InvoiceCode}: {invoiceDescription}";
    }

    private static bool ShouldCreateOwnerUtilityJournalEntryForBill(Receipt bill)
    {
        if (!bill.IsUtility)
            return false;

        var eligibleSplits = ResolveReceiptSplitLines(bill)
            .Where(IsJournalEligibleReceiptSplit)
            .ToList();
        if (eligibleSplits.Count == 0)
            return false;

        // Owner utility JE should be created per eligible split. NonExpense splits are ignored, so a
        // mixed Owner + NonExpense bill still generates owner utility entries for the owner portion.
        return eligibleSplits.All(split => split.ReceiptType == ReceiptType.Owner);
    }

    private static string BuildOwnerUtilityReceiptMemo(string receiptCode, IEnumerable<ReceiptSplit> ownerSplitLines)
    {
        var ownerSplitDescription = ownerSplitLines
            .Select(split => split.Description)
            .FirstOrDefault(description => !string.IsNullOrWhiteSpace(description));
        var description = (ownerSplitDescription ?? string.Empty).Trim();
        return string.IsNullOrWhiteSpace(description)
            ? $"Owner: Utiltiy: {receiptCode}"
            : $"Owner: Utiltiy: {receiptCode}: {description}";
    }

    private async Task<decimal> GetOwnerPercentageBaseAsync(Invoice invoice)
    {
        // The owner's percentage is taken on rent PLUS the recurring monthly charges that follow rent
        // across accounting periods (e.g. Security Deposit Waiver and monthly fees posted to the rent
        // account). One-time fees (security deposit, departure fee, maid service) are excluded. For a
        // cross-period slice these lines already hold the slice's apportioned amount, so the two
        // slices sum back to the full invoice base.
        var total = TryGetInvoiceRentalLineAmount(invoice, out var rentalAmount) ? rentalAmount : 0m;

        var reservation = invoice.ReservationId.HasValue
            ? await _reservationRepository.GetReservationByIdAsync(invoice.ReservationId.Value, invoice.OrganizationId)
            : null;
        if (reservation == null)
            return total;

        var recurringTotal = (await GetApportionableIncomeChargeLinesAsync(invoice, reservation))
            .Concat(GetSplitPoolExtraFeeLines(invoice, reservation))
            .GroupBy(l => (l.CostCodeId, l.Description))
            .Select(g => g.First())
            .Sum(l => l.Amount);

        return total + recurringTotal;
    }

    private async Task<decimal> GetProratedOwnerFlatAmountAsync(Invoice invoice, decimal flatAmount)
    {
        // The owner's flat (and minimum) amount is a full-month figure. When the tenant only stays
        // part of the month, the owner is paid pro rata on the same 30-day basis used for rent — e.g.
        // a 3000 flat for a 5-day stay pays the owner 500. A full month pays the entire flat amount.
        if (flatAmount == 0)
            return 0m;

        if (!TryGetInvoiceRentalLedgerLine(invoice, out var rentalLine))
            return flatAmount;

        var referenceYear = invoice.AccountingPeriod != default
            ? invoice.AccountingPeriod.Year
            : invoice.InvoiceDate.Year;

        if (!TryParseRentalFeeDateRange(rentalLine.Description, referenceYear, out var rentalStart, out var rentalEnd))
            return flatAmount;

        var reservation = invoice.ReservationId.HasValue
            ? await _reservationRepository.GetReservationByIdAsync(invoice.ReservationId.Value, invoice.OrganizationId)
            : null;
        var billingType = reservation?.BillingType ?? BillingType.Monthly;
        var departureDate = reservation?.DepartureDate ?? rentalEnd;

        var days = CalculateNumberOfDays(
            rentalStart,
            rentalEnd,
            billingType,
            IsDepartureMonthYear(rentalEnd, departureDate),
            IsLastDayOfMonth(rentalEnd));

        var daysInMonth = DateTime.DaysInMonth(rentalStart.Year, rentalStart.Month);

        if (days > 0 && days < daysInMonth && days < PRORATE_DAYS)
            return Math.Round(flatAmount / PRORATE_DAYS * days, 2, MidpointRounding.AwayFromZero);

        return flatAmount;
    }

    private static bool ShouldCreateJournalEntryForWorkOrder(WorkOrder workOrder)
        => workOrder.WorkOrderType is WorkOrderType.Tenant or WorkOrderType.Owner or WorkOrderType.Company;

    private async Task<JournalEntry> BuildJournalEntryFromWorkOrderAsync(WorkOrder workOrder, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // WORK-ORDER-JE-ACCOUNTS
        // Base lines (all work orders):
        //   Line 1 — Debit: Expense by work order type (Tenant => GetDefaultTenantExpense, Owner => GetDefaultOwnerExpense, Company => GetDefaultCompanyExpense).
        //   Line 2 — Credit: Accounts Payable (GetDefaultAccountsPayable).
        // Owner lines (only when at least one linked receipt split is Owner type):
        //   Line 3 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable).
        //   Line 4 — Credit: Owner Income (GetDefaultOwnerIncome).
        // Negative amounts reverse debit/credit direction with explicit sign handling.
        // END WORK-ORDER-JE-ACCOUNTS

        if (workOrder.WorkOrderDate == default)
            throw new Exception("WorkOrderDate is required to create a journal entry for a work order");

        var workOrderAmount = workOrder.Amount != 0
            ? workOrder.Amount
            : (workOrder.WorkOrderItems ?? new List<WorkOrderItem>()).Sum(item => item.ItemAmount);
        if (workOrderAmount == 0)
            throw new Exception("WorkOrder amount cannot be zero when creating a journal entry");
        var receiptSplits = await GetEligibleWorkOrderReceiptSplitsAsync(workOrder);

        var workOrderExpenseAccountId = workOrder.WorkOrderType switch
        {
            WorkOrderType.Owner => GetDefaultOwnerExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice),
            WorkOrderType.Company => GetDefaultCompanyExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice),
            _ => GetDefaultTenantExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice)
        };
        var accountsPayableAccountId = GetDefaultAccountsPayable(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerIncomeAccountId = GetDefaultOwnerIncome(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerContactId = await ResolveWorkOrderOwnerContactIdAsync(workOrder);
        var hasOwnerSplit = receiptSplits.Any(split => split.ReceiptType == ReceiptType.Owner);
        var workOrderLabel = string.IsNullOrWhiteSpace(workOrder.WorkOrderCode) ? workOrder.WorkOrderId.ToString() : workOrder.WorkOrderCode.Trim();
        Guid? propertyId = workOrder.PropertyId == Guid.Empty ? null : workOrder.PropertyId;
        var primarySplit = receiptSplits.FirstOrDefault();
        var memoType = primarySplit != null
            ? ResolveWorkOrderMemoType(primarySplit.ReceiptType)
            : ResolveWorkOrderMemoType(workOrder.WorkOrderType);
        var memo = BuildWorkOrderTypedMemo(memoType, workOrderLabel, primarySplit?.Description ?? workOrder.Description);
        var ownerMemo = BuildWorkOrderTypedMemo("Owner", workOrderLabel, primarySplit?.Description ?? workOrder.Description);
        var mainEntryAmount = receiptSplits.Count > 0 ? receiptSplits.Sum(split => split.Amount) : workOrderAmount;
        var journalEntryLines = new List<JournalEntryLine>();

        if (receiptSplits.Count > 0)
        {
            foreach (var split in receiptSplits)
            {
                var expenseAccountId = split.ChartOfAccountId > 0
                    ? split.ChartOfAccountId.Value
                    : split.ReceiptType switch
                    {
                        ReceiptType.Owner => GetDefaultOwnerExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice),
                        ReceiptType.Company => GetDefaultCompanyExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice),
                        ReceiptType.Departure => GetDefaultDepartureExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice),
                        _ => GetDefaultTenantExpense(chartOfAccounts, workOrder.OfficeId, accountingOffice)
                    };
                var splitMemo = BuildWorkOrderTypedMemo(ResolveWorkOrderMemoType(split.ReceiptType), workOrderLabel, split.Description);
                journalEntryLines.Add(new JournalEntryLine
                {
                    ChartOfAccountId = expenseAccountId,
                    PropertyId = propertyId,
                    ReservationId = workOrder.ReservationId,
                    ContactId = ownerContactId,
                    Debit = split.Amount > 0 ? split.Amount : 0,
                    Credit = split.Amount < 0 ? Math.Abs(split.Amount) : 0,
                    Memo = splitMemo,
                    CreatedBy = currentUser
                });
            }
        }
        else
        {
            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = workOrderExpenseAccountId,
                PropertyId = propertyId,
                ReservationId = workOrder.ReservationId,
                ContactId = ownerContactId,
                Debit = workOrderAmount > 0 ? workOrderAmount : 0,
                Credit = workOrderAmount < 0 ? Math.Abs(workOrderAmount) : 0,
                Memo = ownerMemo,
                CreatedBy = currentUser
            });
        }

        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = accountsPayableAccountId,
            PropertyId = propertyId,
            ReservationId = workOrder.ReservationId,
            ContactId = ownerContactId,
            Debit = mainEntryAmount < 0 ? Math.Abs(mainEntryAmount) : 0,
            Credit = mainEntryAmount > 0 ? mainEntryAmount : 0,
            Memo = memo,
            CreatedBy = currentUser
        });

        if (hasOwnerSplit)
        {
            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = ownerAccountsPayableAccountId,
                PropertyId = propertyId,
                ReservationId = workOrder.ReservationId,
                ContactId = ownerContactId,
                Debit = workOrderAmount > 0 ? workOrderAmount : 0,
                Credit = workOrderAmount < 0 ? Math.Abs(workOrderAmount) : 0,
                Memo = memo,
                CreatedBy = currentUser
            });

            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = ownerIncomeAccountId,
                PropertyId = propertyId,
                ReservationId = workOrder.ReservationId,
                ContactId = ownerContactId,
                Debit = workOrderAmount < 0 ? Math.Abs(workOrderAmount) : 0,
                Credit = workOrderAmount > 0 ? workOrderAmount : 0,
                Memo = ownerMemo,
                CreatedBy = currentUser
            });
        }

        return new JournalEntry
        {
            OrganizationId = workOrder.OrganizationId,
            OfficeId = workOrder.OfficeId,
            TransactionDate = workOrder.WorkOrderDate,
            PostingDate = workOrder.WorkOrderDate,
            SourceTypeId = (int)SourceType.WorkOrder,
            SourceId = workOrder.WorkOrderId,
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }

    private async Task<Guid?> ResolveWorkOrderOwnerContactIdAsync(WorkOrder workOrder)
    {
        if (workOrder.PropertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(workOrder.PropertyId, workOrder.OrganizationId);
        if (property?.Owner1Id is { } ownerId && ownerId != Guid.Empty)
            return ownerId;

        return null;
    }

    private async Task<List<ReceiptSplit>> GetEligibleWorkOrderReceiptSplitsAsync(WorkOrder workOrder)
    {
        var linkedReceiptIds = (workOrder.WorkOrderItems ?? new List<WorkOrderItem>())
            .Select(item => item.ReceiptId)
            .Where(receiptId => receiptId is { } id && id != Guid.Empty)
            .Select(receiptId => receiptId!.Value)
            .Distinct()
            .ToList();
        if (linkedReceiptIds.Count == 0)
            return new List<ReceiptSplit>();

        var linkedReceipts = await Task.WhenAll(linkedReceiptIds.Select(receiptId => _maintenanceRepository.GetReceiptByIdAsync(receiptId, workOrder.OrganizationId)));
        return linkedReceipts
            .Where(receipt => receipt != null)
            .SelectMany(receipt => ResolveReceiptSplitLines(receipt!))
            .Where(IsJournalEligibleReceiptSplit)
            .ToList();
    }

    private static string BuildWorkOrderTypedMemo(string typeLabel, string workOrderCode, string? splitDescription)
    {
        var description = (splitDescription ?? string.Empty).Trim();
        if (string.Equals(typeLabel, "Owner", StringComparison.OrdinalIgnoreCase))
        {
            return string.IsNullOrWhiteSpace(description)
                ? $"Owner: {workOrderCode}"
                : $"Owner: {workOrderCode}: {description}";
        }

        return string.IsNullOrWhiteSpace(description)
            ? $"{typeLabel}: WorkOrder: {workOrderCode}"
            : $"{typeLabel}: WorkOrder: {workOrderCode}: {description}";
    }

    private static string ResolveWorkOrderMemoType(WorkOrderType workOrderType)
    {
        return workOrderType switch
        {
            WorkOrderType.Owner => "Owner",
            WorkOrderType.Company => "Company",
            _ => "Tenant"
        };
    }

    private static string ResolveWorkOrderMemoType(ReceiptType receiptType)
    {
        return receiptType switch
        {
            ReceiptType.Owner => "Owner",
            ReceiptType.Company => "Company",
            _ => "Tenant"
        };
    }
