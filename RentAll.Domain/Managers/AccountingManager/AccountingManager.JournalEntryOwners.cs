    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromWorkOrderWithResultAsync(WorkOrder workOrder, Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(workOrder.OrganizationId))
            return AccountingJournalEntryResult.Success();

        if (!ShouldCreateJournalEntryForWorkOrder(workOrder))
            return AccountingJournalEntryResult.Success();

        try
        {
            var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(workOrder.OrganizationId, workOrder.OfficeId);
            var journalEntry = await BuildJournalEntryFromWorkOrderAsync(workOrder, chartOfAccounts, accountingOffice, currentUser);
            var created = await CreateAutoGeneratedJournalEntryAsync(journalEntry);
            return AccountingJournalEntryResult.Success(created);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "WorkOrder",
                organizationId: workOrder.OrganizationId,
                officeId: workOrder.OfficeId,
                sourceTypeId: (int)SourceType.WorkOrder,
                sourceId: workOrder.WorkOrderId,
                documentCode: workOrder.WorkOrderCode,
                accountingPeriod: null,
                amount: null,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    private async Task<JournalEntry?> CreateJournalEntryFromInvoiceOwnerShareAsync(Invoice invoice, decimal amount, Guid currentUser)
    {
        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (propertyId == null || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        var propertyAgreement = await _propertyRepository.GetPropertyAgreementByPropertyIdAsync(propertyId.Value);
        if (property == null || propertyAgreement == null)
            return null;

        var accountContextTask = LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var officeCostCodeContextTask = LoadOfficeCostCodeContextAsync(invoice.OrganizationId, invoice.OfficeId);
        await Task.WhenAll(accountContextTask, officeCostCodeContextTask);
        var (chartOfAccounts, accountingOffice) = await accountContextTask;
        var (office, costCodeById) = await officeCostCodeContextTask;

        decimal ownerAmount = 0m;
        List<JournalEntryLine>? journalEntryLines = new List<JournalEntryLine>();
        switch (property.PropertyLeaseType)
        {
            case PropertyLeaseType.PropertyManagement:
            {
                if (!TryGetInvoiceRentalLedgerLine(invoice, out _))
                    return null;

                switch (propertyAgreement.ManagementFeeType)
                {
                    case ManagementFeeType.FlatRate:
                        ownerAmount = await GetProratedOwnerFlatAmountAsync(invoice, propertyAgreement.FlatRateAmount);
                        break;

                    case ManagementFeeType.Percentage:
                        ownerAmount = amount * propertyAgreement.RevenueSplitOwner / 100m;
                        break;

                    case ManagementFeeType.Minimum:
                    {
                        ownerAmount = amount * propertyAgreement.RevenueSplitOwner / 100m;
                        var proratedMinimum = await GetProratedOwnerFlatAmountAsync(invoice, propertyAgreement.FlatRateAmount);
                        if (ownerAmount < proratedMinimum)
                            ownerAmount = proratedMinimum;
                        break;
                    }

                    default:
                        break;
                }

                if (ownerAmount != 0)
                    journalEntryLines = BuildJournalEntryLinesForPropertyManagement(invoice, ownerAmount, property, chartOfAccounts, accountingOffice, office, costCodeById, propertyId.Value, currentUser);

                break;
            }

            case PropertyLeaseType.Direct:
            case PropertyLeaseType.ThirdParty:
            {
                return null;
            }

            default:
                return null;
        }

        if (journalEntryLines == null || journalEntryLines.Count == 0)
            return null;

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner share journal entry for an invoice");

        var ownerJournalEntry = new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = invoice.AccountingPeriod,
            PostingDate = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            Memo = $"Owner Share - {invoice.InvoiceCode}",
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };

        return await CreateAutoGeneratedJournalEntryAsync(ownerJournalEntry);
    }
