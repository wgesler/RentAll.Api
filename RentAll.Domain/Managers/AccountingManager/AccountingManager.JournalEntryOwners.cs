    private async Task<decimal?> CalculateOwnerActualAmountForInvoicePaymentAsync(Invoice invoice, decimal paymentAmount)
    {
        if (paymentAmount == 0)
            return null;

        var ownerShareInvoice = await ResolveInvoiceForOwnerShareAsync(invoice);

        if (!TryGetInvoiceRentalLedgerLine(ownerShareInvoice, out _))
            return null;

        var rentPlus4000Base = await GetInvoiceRentPlus4000BaseAsync(ownerShareInvoice);
        if (rentPlus4000Base == 0)
            return null;

        var securityDepositBase = await GetInvoiceSecurityDepositBaseAsync(ownerShareInvoice);
        var sdwBase = await GetInvoiceSecurityDepositWaiverBaseAsync(ownerShareInvoice);
        var feesBase = await GetInvoiceFeesBaseAsync(ownerShareInvoice);
        var invoiceTotal = rentPlus4000Base + securityDepositBase + sdwBase + feesBase;
        if (invoiceTotal == 0)
            return null;

        var nonRentPaymentAmount = securityDepositBase + sdwBase + feesBase;

        var rentPaymentAmount = paymentAmount <= nonRentPaymentAmount
            ? 0m
            : Math.Min(paymentAmount - nonRentPaymentAmount, rentPlus4000Base);

        if (rentPaymentAmount == 0)
            return await TryGetOwnerExpectedAmountForInvoicePeriodAsync(invoice);

        var ownerAmount = await CalculateOwnerShareAmountForInvoiceAsync(ownerShareInvoice, rentPaymentAmount);
        if (!ownerAmount.HasValue || ownerAmount.Value == 0)
            return await TryGetOwnerExpectedAmountForInvoicePeriodAsync(invoice);

        return ownerAmount;
    }

    private async Task<decimal?> ResolveOwnerActualAmountForSliceAsync(
        Invoice sliceInvoice,
        decimal slicePaymentAmount,
        IReadOnlyList<JournalEntry>? workingEntries)
    {
        if (slicePaymentAmount == 0 || sliceInvoice.AccountingPeriod == default)
            return null;

        var ownerExpectedAmount = TryGetOwnerExpectedAmountFromEntries(workingEntries ?? [], sliceInvoice)
            ?? await TryGetOwnerExpectedAmountForInvoicePeriodAsync(sliceInvoice);
        if (!ownerExpectedAmount.HasValue || ownerExpectedAmount.Value == 0)
            return await CalculateOwnerActualAmountForInvoicePaymentAsync(sliceInvoice, slicePaymentAmount);

        // Collected slice → OwnAct is the OwnRent (Owner Expected) for that period.
        var postedPeriodArCharge = await TryGetPostedInvoiceArChargeAmountForAccountingPeriodAsync(sliceInvoice);
        if (postedPeriodArCharge is > 0 && slicePaymentAmount >= postedPeriodArCharge.Value)
            return ownerExpectedAmount;

        var ownerShareInvoice = await ResolveInvoiceForOwnerShareAsync(sliceInvoice);
        var sliceChargeTotal = await GetInvoiceChargeTotalForOwnerActualAsync(ownerShareInvoice);
        if (sliceChargeTotal > 0 && slicePaymentAmount >= sliceChargeTotal)
            return ownerExpectedAmount;

        var sliceRentBase = postedPeriodArCharge ?? await GetInvoiceRentPlus4000BaseAsync(ownerShareInvoice);
        if (sliceRentBase <= 0)
            return ownerExpectedAmount;

        if (slicePaymentAmount >= sliceRentBase)
            return ownerExpectedAmount;

        return Math.Min(
            ownerExpectedAmount.Value,
            Math.Round(ownerExpectedAmount.Value * slicePaymentAmount / sliceRentBase, 2, MidpointRounding.AwayFromZero));
    }

    private async Task<decimal?> TryGetPostedInvoiceArChargeAmountForAccountingPeriodAsync(Invoice invoice)
    {
        if (invoice.AccountingPeriod == default)
            return null;

        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var accountsReceivableAccountId = GetDefaultAccountsReceivable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        if (accountsReceivableAccountId <= 0)
            return null;

        var sourceJournalEntries = (await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
        {
            OrganizationId = invoice.OrganizationId,
            OfficeIds = invoice.OfficeId.ToString(),
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            IncludeVoided = false,
            IncludeUnposted = true
        })).ToList();

        var arAmount = sourceJournalEntries
            .Where(entry => entry.JournalEntryKindId == JournalEntryKind.Charge
                && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod))
            .SelectMany(entry => entry.JournalEntryLines)
            .Where(line => line.ChartOfAccountId == accountsReceivableAccountId)
            .Sum(line => line.Debit - line.Credit);

        arAmount = Math.Abs(arAmount);
        return arAmount == 0 ? null : arAmount;
    }

    private async Task<decimal> GetInvoiceChargeTotalForOwnerActualAsync(Invoice invoice)
    {
        var rentPlus4000Base = await GetInvoiceRentPlus4000BaseAsync(invoice);
        var securityDepositBase = await GetInvoiceSecurityDepositBaseAsync(invoice);
        var sdwBase = await GetInvoiceSecurityDepositWaiverBaseAsync(invoice);
        var feesBase = await GetInvoiceFeesBaseAsync(invoice);
        return rentPlus4000Base + securityDepositBase + sdwBase + feesBase;
    }

    private static decimal? TryGetOwnerExpectedAmountFromEntries(IEnumerable<JournalEntry> entries, Invoice invoice)
    {
        var ownerExpected = entries.FirstOrDefault(entry =>
            entry.JournalEntryKindId == JournalEntryKind.OwnerExpected
            && entry.SourceId == invoice.InvoiceId
            && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));
        if (ownerExpected == null)
            return null;

        // Balanced JE: use AP credit (same as DB lookup), not Credit-Debit across all lines (that is always 0).
        var amount = ownerExpected.JournalEntryLines.Sum(line => line.Credit);
        if (amount == 0)
            amount = Math.Abs(ownerExpected.JournalEntryLines.Sum(line => line.Debit));
        return amount == 0 ? null : amount;
    }

    private async Task<decimal?> TryGetOwnerExpectedAmountForInvoicePeriodAsync(Invoice invoice)
    {
        if (invoice.AccountingPeriod == default)
            return null;

        // OwnerExpected is not cash-only; GetByCriteria is fine. Prefer Kind over memo.
        var entries = (await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
        {
            OrganizationId = invoice.OrganizationId,
            OfficeIds = invoice.OfficeId.ToString(),
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            IncludeVoided = false,
            IncludeUnposted = true
        })).ToList();

        var ownerExpected = entries.FirstOrDefault(entry =>
            entry.JournalEntryKindId == JournalEntryKind.OwnerExpected
            && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));
        if (ownerExpected == null)
            return null;

        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var amount = ownerExpected.JournalEntryLines
            .Where(line => line.ChartOfAccountId == ownerAccountsPayableAccountId)
            .Sum(line => line.Credit - line.Debit);

        return amount == 0 ? null : amount;
    }

    private async Task<JournalEntry?> BuildJournalEntryFromBillForOwnerUtilityAsync(Receipt bill, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // BILL-OWNER-UTILITY-JE-ACCOUNTS
        // Trigger condition: Bill IsUtility = true and the bill split set is Owner type.
        // Line 1 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable) for owner utility total.
        // Line 2 — Credit: PM Utility Income (GetDefaultPmUtilityIncome) for owner utility total.
        // Negative owner utility totals (bill credits) reverse debit/credit direction.
        // END BILL-OWNER-UTILITY-JE-ACCOUNTS

        bill = await LoadReceiptWithSplitsAsync(bill);

        if (!ShouldCreateOwnerUtilityJournalEntryForBill(bill))
            return null;

        EnsureBillIsUtility(bill);

        if (bill.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner utility journal entry for a bill");

        if (bill.ReceiptDate == default)
            throw new Exception("ReceiptDate is required to create an owner utility journal entry for a bill");

        var ownerSplitLines = ResolveReceiptSplitLines(bill)
            .Where(split => split.ReceiptType == ReceiptType.Owner)
            .Where(split => split.Amount != 0)
            .ToList();

        if (ownerSplitLines.Count == 0)
            return null;

        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, bill.OfficeId, accountingOffice);
        var pmUtilityIncomeAccountId = GetDefaultPmUtilityIncome(chartOfAccounts, bill.OfficeId, accountingOffice);
        var billLabel = !string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.BillNumber.Trim() : bill.ReceiptCode.Trim();
        var receiptCode = bill.ReceiptCode.Trim();
        var propertyId = ownerSplitLines
            .Select(split => split.PropertyId)
            .FirstOrDefault(splitPropertyId => splitPropertyId is { } id && id != Guid.Empty)
            ?? bill.PropertyIds.FirstOrDefault(id => id != Guid.Empty);
        Guid? ownerContactId = null;
        string? propertyCode = null;
        string? ownerContactName = null;
        if (propertyId != Guid.Empty)
        {
            var property = await _propertyRepository.GetPropertyByIdAsync(propertyId, bill.OrganizationId);
            propertyCode = NormalizeOptionalString(property?.PropertyCode);
            if (property?.Owner1Id is { } resolvedOwnerId && resolvedOwnerId != Guid.Empty)
            {
                ownerContactId = resolvedOwnerId;
                var contact = await _contactRepository.GetContactByIdAsync(resolvedOwnerId, bill.OrganizationId);
                ownerContactName = NormalizeOptionalString(contact?.DisplayName ?? contact?.CompanyName ?? contact?.FullName);
            }
        }
        var ownerSplitDescription = ownerSplitLines
            .Select(split => split.Description)
            .FirstOrDefault(description => !string.IsNullOrWhiteSpace(description));
        var memo = BuildOwnerBillMemo(receiptCode, ownerSplitDescription ?? string.Empty);
        var journalEntryLines = new List<JournalEntryLine>();
        foreach (var split in ownerSplitLines)
        {
            var splitAmount = split.Amount;
            var splitPropertyId = split.PropertyId is { } candidatePropertyId && candidatePropertyId != Guid.Empty
                ? candidatePropertyId
                : propertyId;
            var splitMemo = BuildOwnerBillMemo(receiptCode, split.Description ?? string.Empty);
            var splitIncomeMemo = BuildBillMemo(receiptCode, split.Description ?? string.Empty);
            var splitContext = new JournalEntryLineContext(
                NormalizeOptionalGuid(splitPropertyId == Guid.Empty ? null : splitPropertyId),
                propertyCode,
                null,
                null,
                ownerContactId,
                ownerContactName);
            var ownerPayableLine = new JournalEntryLine
            {
                ChartOfAccountId = ownerAccountsPayableAccountId,
                Debit = splitAmount > 0 ? splitAmount : 0,
                Credit = splitAmount < 0 ? Math.Abs(splitAmount) : 0,
                Memo = splitMemo,
                CreatedBy = currentUser
            };
            ApplyJournalEntryLineContext(ownerPayableLine, splitContext);
            journalEntryLines.Add(ownerPayableLine);
            var utilityIncomeLine = new JournalEntryLine
            {
                ChartOfAccountId = pmUtilityIncomeAccountId,
                Debit = splitAmount < 0 ? Math.Abs(splitAmount) : 0,
                Credit = splitAmount > 0 ? splitAmount : 0,
                Memo = splitIncomeMemo,
                CreatedBy = currentUser
            };
            ApplyJournalEntryLineContext(utilityIncomeLine, splitContext);
            journalEntryLines.Add(utilityIncomeLine);
        }

        return ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = bill.OrganizationId,
            OfficeId = bill.OfficeId,
            TransactionDate = ResolveBillOrReceiptJournalEntryDate(bill),
            SourceTypeId = (int)SourceType.Bill,
            SourceId = bill.ReceiptId,
            SourceCode = ResolveJournalEntrySourceCodeFromReceipt(bill),
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        }, JournalEntryKind.OwnerUtility, Perspective.Owner);
    }

    private async Task<JournalEntry> BuildJournalEntryFromWorkOrderAsync(WorkOrder workOrder, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // WORK-ORDER-JE-ACCOUNTS
        // Base lines are intentionally NOT created here.
        // The linked bill/receipt flow already posts the base expense/AP journal entry.
        //
        // This work-order journal entry creates owner transfer lines only, using the work order's own
        // item amounts (no receipt re-splitting):
        //   Line 3 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable) for the owner total.
        //   Line 4+ — Credit: Owner Income (GetDefaultOwnerIncome) per work-order item amount.
        // Negative amounts reverse debit/credit direction with explicit sign handling.
        // END WORK-ORDER-JE-ACCOUNTS

        if (workOrder.WorkOrderDate == default)
            throw new Exception("WorkOrderDate is required to create a journal entry for a work order");

        var workOrderAmount = workOrder.Amount != 0 ? workOrder.Amount
            : (workOrder.WorkOrderItems ?? new List<WorkOrderItem>()).Sum(item => item.ItemAmount);
        var ownerWorkOrderLines = GetEligibleWorkOrderOwnerLines(workOrder);
        var ownerWorkOrderTotal = ownerWorkOrderLines.Sum(line => line.ItemAmount);

        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerIncomeAccountId = GetDefaultOwnerIncome(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerContactId = await ResolveWorkOrderOwnerContactIdAsync(workOrder);
        string? ownerContactName = null;
        if (ownerContactId is { } resolvedOwnerContactId && resolvedOwnerContactId != Guid.Empty)
        {
            var contact = await _contactRepository.GetContactByIdAsync(resolvedOwnerContactId, workOrder.OrganizationId);
            ownerContactName = NormalizeOptionalString(contact?.DisplayName ?? contact?.CompanyName ?? contact?.FullName);
        }

        var lineContext = CreateJournalEntryLineContextFromWorkOrder(workOrder, ownerContactId, ownerContactName);
        var memo = BuildOwnerWorkOrderMemo(workOrder.WorkOrderCode, workOrder.Title);
        var journalEntryLines = new List<JournalEntryLine>();

        if (workOrder.WorkOrderType == WorkOrderType.Owner && ownerWorkOrderTotal != 0)
        {
            var ownerPayableLine = new JournalEntryLine
            {
                ChartOfAccountId = ownerAccountsPayableAccountId,
                Debit = ownerWorkOrderTotal > 0 ? ownerWorkOrderTotal : 0,
                Credit = ownerWorkOrderTotal < 0 ? Math.Abs(ownerWorkOrderTotal) : 0,
                Memo = memo,
                CreatedBy = currentUser
            };
            ApplyJournalEntryLineContext(ownerPayableLine, lineContext);
            journalEntryLines.Add(ownerPayableLine);

            foreach (var ownerLine in ownerWorkOrderLines)
            {
                var lineAmount = ownerLine.ItemAmount;
                var lineMemo = BuildOwnerWorkOrderMemo(workOrder.WorkOrderCode, ownerLine.Description ?? string.Empty);
                var ownerIncomeLine = new JournalEntryLine
                {
                    ChartOfAccountId = ownerIncomeAccountId,
                    Debit = lineAmount < 0 ? Math.Abs(lineAmount) : 0,
                    Credit = lineAmount > 0 ? lineAmount : 0,
                    Memo = lineMemo,
                    CreatedBy = currentUser
                };
                ApplyJournalEntryLineContext(ownerIncomeLine, lineContext);
                journalEntryLines.Add(ownerIncomeLine);
            }
        }

        return ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = workOrder.OrganizationId,
            OfficeId = workOrder.OfficeId,
            TransactionDate = workOrder.WorkOrderDate,
            AccountingPeriod = workOrder.AccountingPeriod == default
                ? FirstDayOfMonth(workOrder.WorkOrderDate)
                : workOrder.AccountingPeriod,
            SourceTypeId = (int)SourceType.WorkOrder,
            SourceId = workOrder.WorkOrderId,
            SourceCode = ResolveJournalEntrySourceCodeFromWorkOrder(workOrder),
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        },
            workOrder.WorkOrderType == WorkOrderType.Owner && journalEntryLines.Count > 0
                ? JournalEntryKind.OwnerTransfer
                : JournalEntryKind.Expense,
            (Perspective)(int)workOrder.WorkOrderType);
    }
