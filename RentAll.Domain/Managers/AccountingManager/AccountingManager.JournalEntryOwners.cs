    #region Journal Entry
    private List<JournalEntryLine> BuildJournalEntryForOwnerShare(Invoice invoice, decimal ownerAmount, Property property, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Office? office, IReadOnlyDictionary<int, CostCode> costCodeById, Guid propertyId, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // OWNER-SHARE-JE-ACCOUNTS
        //   Line 1 — Debit: Tenant rental expense for the invoice rental ledger line.
        //   Line 2 — Credit: Default Own.
        // END OWNER-SHARE-JE-ACCOUNTS

        var journalEntryLines = new List<JournalEntryLine>();

        if (!TryGetInvoiceRentalLedgerLine(invoice, out var rentalLine))
            return journalEntryLines;

        var isFurnished = !property.Unfurnished;
        var memo = BuildOwnerShareInvoiceMemo(invoice);
        var ownerContactId = property.Owner1Id;
        costCodeById.TryGetValue(rentalLine.CostCodeId, out var rentalCostCode);
        var ownerAccountsPayableId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var ownerRentExpenseCostCodeId = isFurnished ? office?.FurnishedRentExpenseCcId : office?.UnfurnishedRentExpenseCcId;
        var ownerRentExpenseAccountId = isFurnished
            ? GetDefaultFurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice)
            : GetDefaultUnfurnishedRentExpense(chartOfAccounts, invoice.OfficeId, office, costCodeById, accountingOffice);

        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = ownerRentExpenseAccountId,
            CostCodeId = rentalLine.CostCodeId,
            PropertyId = propertyId,
            ReservationId = invoice.ReservationId,
            ContactId = ownerContactId,
            Debit = ownerAmount > 0 ? ownerAmount : 0,
            Credit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        });

        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = ownerAccountsPayableId,
            CostCodeId = ownerRentExpenseCostCodeId is > 0 ? ownerRentExpenseCostCodeId : null,
            PropertyId = propertyId,
            ReservationId = invoice.ReservationId,
            ContactId = ownerContactId,
            Debit = ownerAmount < 0 ? Math.Abs(ownerAmount) : 0,
            Credit = ownerAmount > 0 ? ownerAmount : 0,
            Memo = memo,
            CreatedBy = currentUser
        });

        return journalEntryLines;
    }

    private async Task<JournalEntry?> BuildJournalEntryForOwnerSharePaymentAsync(Invoice invoice, LedgerLine paymentLedgerLine, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        if (paymentLedgerLine.Amount == 0)
            return null;

        // Get the total ledger lines that apply to rent
        var invoicedAmount = await GeRentalLedgerLineBaseAsync(invoice);
        if (invoicedAmount == 0)
            return null;

        // If they paid more than the invoiced amount, just take the invoiced amount
        var paymentAmount = paymentLedgerLine.Amount;
        var paidIncomeMagnitude = Math.Min(Math.Abs(paymentAmount), Math.Abs(invoicedAmount));
        if (paidIncomeMagnitude == 0)
            return null;

        // Make sure we allow credits on invoices
        var paidIncome = paymentAmount < 0 ? -paidIncomeMagnitude : paidIncomeMagnitude;
        var ownerPaymentAmount = await CalculateOwnerShareAmountForInvoiceAsync(invoice, paidIncome);
        if (!ownerPaymentAmount.HasValue || ownerPaymentAmount.Value == 0)
            return null;

        // This gives us the rental income that is applicable to the owner
        var ownerPaymentAmountValue = Math.Round(ownerPaymentAmount.Value, 2, MidpointRounding.AwayFromZero);
        if (ownerPaymentAmountValue == 0)
            return null;

        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (!propertyId.HasValue || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        if (property?.Owner1Id is not { } ownerContactId || ownerContactId == Guid.Empty)
            return null;

        if (paymentLedgerLine.LedgerLineDate == default)
            throw new Exception("Payment date is required to create an owner payment journal entry");

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner payment journal entry");

        var undepositedFundsAccountId = GetDefaultUndepositedFunds(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var reservationId = paymentLedgerLine.ReservationId ?? invoice.ReservationId;

        return new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.InvoicePayment,
            SourceId = paymentLedgerLine.LedgerLineId,
            Memo = BuildOwnerPaymentMemo(invoice),
            JournalEntryLines = new List<JournalEntryLine>
            {
                new()
                {
                    ChartOfAccountId = undepositedFundsAccountId,
                    CostCodeId = paymentLedgerLine.CostCodeId,
                    ReservationId = reservationId,
                    PropertyId = propertyId,
                    ContactId = ownerContactId,
                    Debit = ownerPaymentAmountValue > 0 ? ownerPaymentAmountValue : 0,
                    Credit = ownerPaymentAmountValue < 0 ? Math.Abs(ownerPaymentAmountValue) : 0,
                    Memo = BuildOwnerPaymentMemo(invoice),
                    CreatedBy = currentUser
                },
                new()
                {
                    ChartOfAccountId = ownerAccountsPayableAccountId,
                    ReservationId = reservationId,
                    PropertyId = propertyId,
                    ContactId = ownerContactId,
                    Debit = ownerPaymentAmountValue < 0 ? Math.Abs(ownerPaymentAmountValue) : 0,
                    Credit = ownerPaymentAmountValue > 0 ? ownerPaymentAmountValue : 0,
                    Memo = BuildOwnerPaymentMemo(invoice),
                    CreatedBy = currentUser
                }
            },
            CreatedBy = currentUser
        };
    }

    private async Task<JournalEntry?> BuildJournalEntryFromBillForOwnerUtilityAsync(Receipt bill, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // BILL-OWNER-UTILITY-JE-ACCOUNTS
        // Trigger condition: Bill IsUtility = true and the bill split set is Owner type.
        // Line 1 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable) for owner utility total.
        // Line 2 — Credit: PM Utility Income (GetDefaultPmUtilityIncome) for owner utility total.
        // Negative owner utility totals (bill credits) reverse debit/credit direction.
        // END BILL-OWNER-UTILITY-JE-ACCOUNTS

        EnsureBillIsUtility(bill);
        bill = await LoadReceiptWithSplitsAsync(bill);

        if (!ShouldCreateOwnerUtilityJournalEntryForBill(bill))
            return null;

        if (bill.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner utility journal entry for a bill");

        if (bill.ReceiptDate == default)
            throw new Exception("ReceiptDate is required to create an owner utility journal entry for a bill");

        var ownerSplitLines = ResolveReceiptSplitLines(bill)
            .Where(split => split.ReceiptType == ReceiptType.Owner)
            .Where(split => split.Amount != 0)
            .ToList();

        if (ownerSplitLines.Count == 0)
            return null;

        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, bill.OfficeId, accountingOffice);
        var pmUtilityIncomeAccountId = GetDefaultPmUtilityIncome(chartOfAccounts, bill.OfficeId, accountingOffice);
        var billLabel = !string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.BillNumber.Trim() : bill.ReceiptCode.Trim();
        var receiptCode = bill.ReceiptCode.Trim();
        var propertyId = ownerSplitLines
            .Select(split => split.PropertyId)
            .FirstOrDefault(splitPropertyId => splitPropertyId is { } id && id != Guid.Empty)
            ?? bill.PropertyIds.FirstOrDefault(id => id != Guid.Empty);
        Guid? ownerContactId = null;
        if (propertyId != Guid.Empty)
        {
            var property = await _propertyRepository.GetPropertyByIdAsync(propertyId, bill.OrganizationId);
            if (property?.Owner1Id is { } resolvedOwnerId && resolvedOwnerId != Guid.Empty)
                ownerContactId = resolvedOwnerId;
        }
        var ownerSplitDescription = ownerSplitLines
            .Select(split => split.Description)
            .FirstOrDefault(description => !string.IsNullOrWhiteSpace(description));
        var memo = string.IsNullOrWhiteSpace((ownerSplitDescription ?? string.Empty).Trim())
            ? $"Owner: {receiptCode}"
            : $"Owner: {receiptCode}: {(ownerSplitDescription ?? string.Empty).Trim()}";
        var journalEntryLines = new List<JournalEntryLine>();
        foreach (var split in ownerSplitLines)
        {
            var splitAmount = split.Amount;
            var splitPropertyId = split.PropertyId is { } candidatePropertyId && candidatePropertyId != Guid.Empty
                ? candidatePropertyId
                : propertyId;
            var splitMemo = string.IsNullOrWhiteSpace((split.Description ?? string.Empty).Trim())
                ? $"Owner: {receiptCode}"
                : $"Owner: {receiptCode}: {(split.Description ?? string.Empty).Trim()}";
            var splitIncomeMemo = receiptCode + (string.IsNullOrWhiteSpace((split.Description ?? string.Empty).Trim()) ? string.Empty : $": {(split.Description ?? string.Empty).Trim()}");
            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = ownerAccountsPayableAccountId,
                PropertyId = splitPropertyId == Guid.Empty ? null : splitPropertyId,
                ContactId = ownerContactId,
                Debit = splitAmount > 0 ? splitAmount : 0,
                Credit = splitAmount < 0 ? Math.Abs(splitAmount) : 0,
                Memo = splitMemo,
                CreatedBy = currentUser
            });
            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = pmUtilityIncomeAccountId,
                PropertyId = splitPropertyId == Guid.Empty ? null : splitPropertyId,
                ContactId = ownerContactId,
                Debit = splitAmount < 0 ? Math.Abs(splitAmount) : 0,
                Credit = splitAmount > 0 ? splitAmount : 0,
                Memo = splitIncomeMemo,
                CreatedBy = currentUser
            });
        }

        return new JournalEntry
        {
            OrganizationId = bill.OrganizationId,
            OfficeId = bill.OfficeId,
            TransactionDate = ResolveBillOrReceiptJournalEntryDate(bill),
            SourceTypeId = (int)SourceType.Bill,
            SourceId = bill.ReceiptId,
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }

    private async Task<JournalEntry> BuildJournalEntryFromWorkOrderAsync(WorkOrder workOrder, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // WORK-ORDER-JE-ACCOUNTS
        // Base lines are intentionally NOT created here.
        // The linked bill/receipt flow already posts the base expense/AP journal entry.
        //
        // This work-order journal entry creates owner transfer lines only, using the work order's own
        // item amounts (no receipt re-splitting):
        //   Line 3 — Debit: Owner Accounts Payable (GetDefaultOwnerAccountsPayable) for the owner total.
        //   Line 4+ — Credit: Owner Income (GetDefaultOwnerIncome) per work-order item amount.
        // Negative amounts reverse debit/credit direction with explicit sign handling.
        // END WORK-ORDER-JE-ACCOUNTS

        if (workOrder.WorkOrderDate == default)
            throw new Exception("WorkOrderDate is required to create a journal entry for a work order");

        var workOrderAmount = workOrder.Amount != 0 ? workOrder.Amount
            : (workOrder.WorkOrderItems ?? new List<WorkOrderItem>()).Sum(item => item.ItemAmount);
        var ownerWorkOrderLines = GetEligibleWorkOrderOwnerLines(workOrder);
        var ownerWorkOrderTotal = ownerWorkOrderLines.Sum(line => line.ItemAmount);

        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerIncomeAccountId = GetDefaultOwnerIncome(chartOfAccounts, workOrder.OfficeId, accountingOffice);
        var ownerContactId = await ResolveWorkOrderOwnerContactIdAsync(workOrder);
        Guid? propertyId = workOrder.PropertyId == Guid.Empty ? null : workOrder.PropertyId;
        var memo = BuildOwnerWorkOrderMemo(workOrder.WorkOrderCode, workOrder.Description);
        var journalEntryLines = new List<JournalEntryLine>();

        if (workOrder.WorkOrderType == WorkOrderType.Owner && ownerWorkOrderTotal != 0)
        {
            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = ownerAccountsPayableAccountId,
                PropertyId = propertyId,
                ReservationId = workOrder.ReservationId,
                ContactId = ownerContactId,
                Debit = ownerWorkOrderTotal > 0 ? ownerWorkOrderTotal : 0,
                Credit = ownerWorkOrderTotal < 0 ? Math.Abs(ownerWorkOrderTotal) : 0,
                Memo = memo,
                CreatedBy = currentUser
            });

            foreach (var ownerLine in ownerWorkOrderLines)
            {
                var lineAmount = ownerLine.ItemAmount;
                var lineMemo = BuildOwnerWorkOrderMemo(workOrder.WorkOrderCode, ownerLine.Description);
                journalEntryLines.Add(new JournalEntryLine
                {
                    ChartOfAccountId = ownerIncomeAccountId,
                    PropertyId = propertyId,
                    ReservationId = workOrder.ReservationId,
                    ContactId = ownerContactId,
                    Debit = lineAmount < 0 ? Math.Abs(lineAmount) : 0,
                    Credit = lineAmount > 0 ? lineAmount : 0,
                    Memo = lineMemo,
                    CreatedBy = currentUser
                });
            }
        }

        return new JournalEntry
        {
            OrganizationId = workOrder.OrganizationId,
            OfficeId = workOrder.OfficeId,
            TransactionDate = workOrder.WorkOrderDate,
            SourceTypeId = (int)SourceType.WorkOrder,
            SourceId = workOrder.WorkOrderId,
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }
