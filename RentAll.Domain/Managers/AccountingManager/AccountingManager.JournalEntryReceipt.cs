    #region Receipt And Bill Helpers
    private async Task<Receipt> LoadReceiptWithSplitsAsync(Receipt receipt)
    {
        if (receipt.ReceiptId == Guid.Empty)
            return receipt;

        return await _maintenanceRepository.GetReceiptByIdAsync(receipt.ReceiptId, receipt.OrganizationId)
            ?? receipt;
    }

    private static List<ReceiptSplit> ResolveReceiptSplitLines(Receipt receipt)
    {
        var allSplits = (receipt.Splits ?? new List<ReceiptSplit>())
            .OrderBy(s => s.ReceiptSplitId)
            .ToList();

        var nonZeroSplits = allSplits.Where(split => split.Amount != 0).ToList();
        if (nonZeroSplits.Count > 0)
            return nonZeroSplits;

        if (allSplits.Count > 0)
            return allSplits;

        // Legacy fallback: when no split rows are provided, treat receipt amount as one split.
        if (allSplits.Count == 0 && receipt.Amount != 0)
        {
            return
            [
                new ReceiptSplit
                {
                    Amount = receipt.Amount,
                    Description = receipt.Description,
                    ChartOfAccountId = null
                }
            ];
        }

        return allSplits;
    }

    private static bool IsJournalEligibleReceiptSplit(ReceiptSplit split)
        => split.Amount != 0 && split.ReceiptType != ReceiptType.NonExpense;

    private static string ResolveRequiredReceiptHeaderMemo(string receiptCode, IEnumerable<ReceiptSplit> splitLines)
    {
        var firstEligibleDescription = splitLines
            .Where(IsJournalEligibleReceiptSplit)
            .Select(split => split.Description)
            .FirstOrDefault(description => !string.IsNullOrWhiteSpace(description));
        return BuildRequiredReceiptSplitMemo(receiptCode, firstEligibleDescription);
    }

    private static string BuildRequiredReceiptSplitMemo(string receiptCode, string? splitDescription)
    {
        var description = (splitDescription ?? string.Empty).Trim();
        return string.IsNullOrWhiteSpace(description)
            ? receiptCode
            : $"{receiptCode}: {description}";
    }
