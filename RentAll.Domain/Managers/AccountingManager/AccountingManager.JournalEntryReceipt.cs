    #region Triggers
    public async Task<JournalEntry> CreateJournalEntryFromReceiptAsync(Receipt receipt, Guid currentUser)
    {
        EnsureReceiptIsCardReceipt(receipt);

        var existingEntries = await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
        {
            OrganizationId = receipt.OrganizationId,
            OfficeIds = receipt.OfficeId.ToString(),
            SourceTypeId = (int)SourceType.Receipt,
            SourceId = receipt.ReceiptId,
            IncludeVoided = true,
            IncludeUnposted = true
        });

        var existingEntry = existingEntries.FirstOrDefault(e => !e.IsVoided);
        if (existingEntry != null)
        {
            if (existingEntry.IsPosted)
                return existingEntry;

            await ReplaceJournalEntriesFromReceiptAsync(receipt, currentUser);
            return (await _journalEntryRepository.GetJournalEntryByIdAsync(existingEntry.JournalEntryId, receipt.OrganizationId))!;
        }

        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(receipt.OrganizationId, receipt.OfficeId);
        return await CreateReceiptJournalEntriesAsync(receipt, chartOfAccounts, accountingOffice, currentUser);
    }
    #endregion

    #region Journal Entry
    private async Task<JournalEntry> CreateReceiptJournalEntriesAsync(Receipt receipt, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        var bankCard = await ResolveReceiptBankCardAsync(receipt, accountingOffice);
        var receiptJournalEntry = await BuildJournalEntryFromReceiptAsync(receipt, chartOfAccounts, accountingOffice, currentUser);
        var created = await CreateAutoGeneratedJournalEntryAsync(receiptJournalEntry);

        if (IsCrossOfficeBankCardReceipt(receipt, bankCard))
        {
            var (bankCardChartOfAccounts, bankCardAccountingOffice) = await LoadAccountContextAsync(receipt.OrganizationId, bankCard.OfficeId);
            var receiptOfficeVendorId = await ResolveInterOfficeVendorContactIdAsync(receipt.OrganizationId, bankCard.OfficeId, receipt.OfficeId);
            var receiptOfficeName = await GetOfficeNameAsync(receipt.OrganizationId, receipt.OfficeId);
            var companionEntry = BuildCrossOfficeBankCardLiabilityJournalEntryFromReceiptAsync(receipt, bankCard.OfficeId, bankCardChartOfAccounts, bankCardAccountingOffice, bankCard, receiptOfficeVendorId, receiptOfficeName, currentUser);
            await CreateAutoGeneratedJournalEntryAsync(companionEntry);
        }

        return created;
    }

    private async Task SyncCrossOfficeReceiptCompanionJournalEntryAsync(Receipt receipt, AccountingOffice? receiptAccountingOffice, BankCard bankCard, Guid currentUser)
    {
        await DeleteAllCrossOfficeReceiptCompanionEntriesAsync(receipt);

        if (!IsCrossOfficeBankCardReceipt(receipt, bankCard))
            return;

        var (bankCardChartOfAccounts, bankCardAccountingOffice) = await LoadAccountContextAsync(receipt.OrganizationId, bankCard.OfficeId);
        var receiptOfficeVendorId = await ResolveInterOfficeVendorContactIdAsync(receipt.OrganizationId, bankCard.OfficeId, receipt.OfficeId);
        var receiptOfficeName = await GetOfficeNameAsync(receipt.OrganizationId, receipt.OfficeId);
        var companionEntry = BuildCrossOfficeBankCardLiabilityJournalEntryFromReceiptAsync(receipt, bankCard.OfficeId, bankCardChartOfAccounts, bankCardAccountingOffice, bankCard, receiptOfficeVendorId, receiptOfficeName, currentUser);
        await CreateAutoGeneratedJournalEntryAsync(companionEntry);
    }

    private async Task<JournalEntry> BuildJournalEntryFromReceiptAsync(Receipt receipt, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        EnsureReceiptIsCardReceipt(receipt);
        receipt = await LoadReceiptWithSplitsAsync(receipt);

        if (receipt.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create a journal entry for a receipt");

        var bankCard = await ResolveReceiptBankCardAsync(receipt, accountingOffice);
        if (IsCrossOfficeBankCardReceipt(receipt, bankCard))
        {
            var bankCardOfficeVendorId = await ResolveInterOfficeVendorContactIdAsync(receipt.OrganizationId, receipt.OfficeId, bankCard.OfficeId);
            var bankCardOfficeName = await GetOfficeNameAsync(receipt.OrganizationId, bankCard.OfficeId);
            return BuildCrossOfficeBankCardJournalEntryFromReceiptAsync(receipt, chartOfAccounts, accountingOffice, bankCardOfficeVendorId, bankCardOfficeName, currentUser);
        }

        return BuildSameOfficeBankCardJournalEntryFromReceiptAsync(receipt, chartOfAccounts, accountingOffice, bankCard, currentUser);
    }

    private JournalEntry BuildSameOfficeBankCardJournalEntryFromReceiptAsync(Receipt receipt, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, BankCard bankCard, Guid currentUser)
    {
        var splitLines = ResolveDocumentSplitLines(receipt);
        var creditCardAccountId = GetCreditCardAccountId(bankCard);
        var transactionDate = receipt.AccountingPeriod;
        var postingDate = receipt.AccountingPeriod;
        var receiptLabel = receipt.ReceiptCode.Trim();
        var memo = string.IsNullOrWhiteSpace(receipt.Description)
            ? $"Receipt {receiptLabel}"
            : receipt.Description.Trim();
        var propertyId = receipt.PropertyIds.FirstOrDefault(id => id != Guid.Empty);

        var totalAmount = splitLines.Sum(s => s.Amount);
        var journalEntryLines = new List<JournalEntryLine>();
        var (creditCardDebit, creditCardCredit) = SignedAmountToDebitCredit(totalAmount, positiveIsDebit: false);
        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = creditCardAccountId,
            PropertyId = propertyId == Guid.Empty ? null : propertyId,
            ContactId = receipt.VendorId,
            Debit = creditCardDebit,
            Credit = creditCardCredit,
            Memo = $"Credit Card - {receipt.BankCardDisplayName}".Trim(),
            CreatedBy = currentUser
        });

        foreach (var split in splitLines)
        {
            var expenseAccountId = GetBillReceiptExpenseAccountId(split, receipt.OfficeId, chartOfAccounts, accountingOffice);
            var (expenseDebit, expenseCredit) = SignedAmountToDebitCredit(split.Amount, positiveIsDebit: true);

            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = expenseAccountId,
                PropertyId = propertyId == Guid.Empty ? null : propertyId,
                ContactId = receipt.VendorId,
                Debit = expenseDebit,
                Credit = expenseCredit,
                Memo = split.Amount < 0 && string.IsNullOrWhiteSpace(split.Description)
                    ? $"Receipt Credit - {receiptLabel}"
                    : split.Description,
                CreatedBy = currentUser
            });
        }

        return new JournalEntry
        {
            OrganizationId = receipt.OrganizationId,
            OfficeId = receipt.OfficeId,
            TransactionDate = transactionDate,
            PostingDate = postingDate,
            SourceTypeId = (int)SourceType.Receipt,
            SourceId = receipt.ReceiptId,
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }

    private JournalEntry BuildCrossOfficeBankCardJournalEntryFromReceiptAsync(Receipt receipt, List<ChartOfAccount> receiptChartOfAccounts, AccountingOffice? receiptAccountingOffice, Guid? bankCardOfficeVendorId, string bankCardOfficeName, Guid currentUser)
    {
        var splitLines = ResolveDocumentSplitLines(receipt);
        var accountsPayableAccountId = GetDefaultAccountsPayable(receiptChartOfAccounts, receipt.OfficeId, receiptAccountingOffice);
        var transactionDate = receipt.AccountingPeriod;
        var postingDate = receipt.AccountingPeriod;
        var receiptLabel = receipt.ReceiptCode.Trim();
        var memo = string.IsNullOrWhiteSpace(receipt.Description)
            ? $"Receipt {receiptLabel}"
            : receipt.Description.Trim();
        var propertyId = receipt.PropertyIds.FirstOrDefault(id => id != Guid.Empty);
        var totalAmount = splitLines.Sum(s => s.Amount);
        var journalEntryLines = new List<JournalEntryLine>();

        foreach (var split in splitLines)
        {
            var expenseAccountId = GetBillReceiptExpenseAccountId(split, receipt.OfficeId, receiptChartOfAccounts, receiptAccountingOffice);
            var (expenseDebit, expenseCredit) = SignedAmountToDebitCredit(split.Amount, positiveIsDebit: true);

            journalEntryLines.Add(new JournalEntryLine
            {
                ChartOfAccountId = expenseAccountId,
                PropertyId = propertyId == Guid.Empty ? null : propertyId,
                ContactId = receipt.VendorId,
                Debit = expenseDebit,
                Credit = expenseCredit,
                Memo = split.Amount < 0 && string.IsNullOrWhiteSpace(split.Description)
                    ? $"Receipt Credit - {receiptLabel}"
                    : split.Description,
                CreatedBy = currentUser
            });
        }

        var (accountsPayableCreditDebit, accountsPayableCreditCredit) = SignedAmountToDebitCredit(totalAmount, positiveIsDebit: false);
        journalEntryLines.Add(new JournalEntryLine
        {
            ChartOfAccountId = accountsPayableAccountId,
            PropertyId = propertyId == Guid.Empty ? null : propertyId,
            ContactId = bankCardOfficeVendorId,
            Debit = accountsPayableCreditDebit,
            Credit = accountsPayableCreditCredit,
            Memo = $"Accounts Payable - {bankCardOfficeName}",
            CreatedBy = currentUser
        });

        return new JournalEntry
        {
            OrganizationId = receipt.OrganizationId,
            OfficeId = receipt.OfficeId,
            TransactionDate = transactionDate,
            PostingDate = postingDate,
            SourceTypeId = (int)SourceType.Receipt,
            SourceId = receipt.ReceiptId,
            Memo = memo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }

