    #region Bank Card Helpers
    private async Task<BankCard> ResolveReceiptBankCardAsync(Receipt receipt, AccountingOffice? receiptAccountingOffice)
    {
        if (receipt.BankCardId is not > 0)
            throw new Exception("BankCardId is required to resolve a bank card");

        var bankCard = receiptAccountingOffice?.BankCards
            .FirstOrDefault(card => card.BankCardId == receipt.BankCardId.Value);
        if (bankCard != null)
            return bankCard;

        var offices = await _organizationRepository.GetOfficesByOrganizationIdAsync(receipt.OrganizationId);
        var officeIds = string.Join(',', offices.Select(office => office.OfficeId));
        if (string.IsNullOrWhiteSpace(officeIds))
            throw new Exception("Bank card not found for organization");

        var organizationBankCards = await _accountingRepository.GetBankCardsByOfficeIdsAsync(receipt.OrganizationId, officeIds);
        bankCard = organizationBankCards.FirstOrDefault(card => card.BankCardId == receipt.BankCardId.Value);
        if (bankCard == null)
            throw new Exception("Bank card not found for organization");

        return bankCard;
    }

    private async Task<Guid?> ResolveInterOfficeVendorContactIdAsync(Guid organizationId, int accountingOfficeId, int representedOfficeId)
    {
        var representedOffice = await _organizationRepository.GetOfficeByIdAsync(representedOfficeId, organizationId);
        if (representedOffice == null)
            throw new Exception($"Office {representedOfficeId} was not found");

        var representedOfficeName = representedOffice.Name.Trim();
        if (string.IsNullOrWhiteSpace(representedOfficeName))
            throw new Exception($"Office {representedOfficeId} does not have a name configured");

        var officeContacts = await _contactRepository.GetContactsByOfficeIdAsync(organizationId, accountingOfficeId.ToString());
        var officeVendor = officeContacts
            .Where(contact => contact.EntityType == EntityType.Vendor && contact.IsActive)
            .FirstOrDefault(contact => ContactRepresentsOffice(contact, representedOfficeName));

        if (officeVendor == null)
            throw new Exception($"No vendor contact named {representedOfficeName} is configured in office {accountingOfficeId}");

        return officeVendor.ContactId;
    }

    private async Task<string> GetOfficeNameAsync(Guid organizationId, int officeId)
    {
        var office = await _organizationRepository.GetOfficeByIdAsync(officeId, organizationId);
        if (office == null)
            throw new Exception($"Office {officeId} was not found");

        var officeName = office.Name.Trim();
        if (string.IsNullOrWhiteSpace(officeName))
            throw new Exception($"Office {officeId} does not have a name configured");

        return officeName;
    }

    private async Task DeleteAllCrossOfficeReceiptCompanionEntriesAsync(Receipt receipt)
    {
        var offices = await _organizationRepository.GetOfficesByOrganizationIdAsync(receipt.OrganizationId);
        foreach (var office in offices)
        {
            await DeleteJournalEntriesForSourceAsync(
                receipt.OrganizationId,
                office.OfficeId,
                (int)SourceType.CreditCard,
                receipt.ReceiptId);
        }
    }

    private static bool IsCrossOfficeBankCardReceipt(Receipt receipt, BankCard bankCard)
        => bankCard.OfficeId != receipt.OfficeId;

    private static string GetReceiptOfficeLabel(Receipt receipt)
        => string.IsNullOrWhiteSpace(receipt.OfficeName) ? receipt.ReceiptCode.Trim() : receipt.OfficeName.Trim();

    private static bool ContactRepresentsOffice(Contact contact, string officeName)
    {
        var candidates = new[]
        {
            contact.CompanyName,
            contact.DisplayName,
            contact.FullName,
            $"{contact.FirstName} {contact.LastName}".Trim()
        };

        return candidates.Any(candidate =>
            !string.IsNullOrWhiteSpace(candidate) &&
            string.Equals(candidate.Trim(), officeName, StringComparison.OrdinalIgnoreCase));
    }

    private static int GetCreditCardAccountId(BankCard bankCard)
    {
        if (bankCard.ChartOfAccountId is not > 0)
            throw new Exception("Bank card must have a chart of account configured");

        return bankCard.ChartOfAccountId.Value;
    }

    private static void EnsureReceiptIsCardReceipt(Receipt receipt)
    {
        if (receipt.BankCardId is not > 0)
            throw new Exception("Receipt is not a card receipt");
    }
