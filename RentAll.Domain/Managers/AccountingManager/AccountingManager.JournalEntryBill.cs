    #region Journal Entry
    private async Task<JournalEntry> BuildJournalEntryFromBillAsync(Receipt bill, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // BILL-JE-ACCOUNTS
        // Positive splits:
        //   Credit - Accounts Payable (GetDefaultAccountsPayable) for the split total.
        //   Debit  - Expense account per split as defined by the user they default to the following
        //   (GetDefaultTenantExpense, GetDefaultDepartureExpense, GetDefaultOwnerExpense, or GetDefaultCompanyExpense).
        // Negative splits (bill credits):
        //   Exact opposite
        // END BILL-JE-ACCOUNTS

        EnsureReceiptIsBill(bill);
        bill = await LoadReceiptWithSplitsAsync(bill);

        var splitLines = (bill.Splits ?? new List<ReceiptSplit>())
            .OrderBy(s => s.ReceiptSplitId)
            .ToList();
        if (splitLines.Count == 0)
            throw new Exception("Splits are required to create a journal entry");

        var nonZeroSplits = splitLines.Where(split => split.Amount != 0).ToList();
        if (nonZeroSplits.Count > 0)
            splitLines = nonZeroSplits;

        if (bill.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create a journal entry for a bill");

        if (bill.ReceiptDate == default)
            throw new Exception("ReceiptDate is required to create a journal entry for a bill");

        var accountsPayableAccountId = GetDefaultAccountsPayable(chartOfAccounts, bill.OfficeId, accountingOffice);
        var transactionDate = ResolveBillOrReceiptJournalEntryDate(bill);
        var receiptCode = bill.ReceiptCode.Trim();
        var defaultLineContext = await ResolveReceiptJournalEntryLineContextAsync(bill);
        var eligibleSplits = splitLines
            .Where(split => split.ReceiptType != ReceiptType.NonExpense)
            .Where(split => split.Amount != 0)
            .ToList();
        var billMemo = BuildBillJournalMemo(bill, eligibleSplits);
        var positiveSplits = eligibleSplits.Where(s => s.Amount > 0).ToList();
        var negativeSplits = eligibleSplits.Where(s => s.Amount < 0).ToList();
        var journalEntryLines = new List<JournalEntryLine>();

        var positiveTotal = positiveSplits.Sum(s => s.Amount);
        if (positiveTotal > 0)
        {
            var accountsPayableLine = new JournalEntryLine
            {
                // Credit Accounts Payable
                ChartOfAccountId = accountsPayableAccountId,
                Debit = 0,
                Credit = positiveTotal,
                Memo = billMemo,
                CreatedBy = currentUser
            };
            ApplyJournalEntryLineContext(accountsPayableLine, defaultLineContext);
            journalEntryLines.Add(accountsPayableLine);

            foreach (var split in positiveSplits)
            {
                var expenseAccountId = GetBillReceiptExpenseAccountId(split, bill.OfficeId, chartOfAccounts, accountingOffice);
                var splitMemo = BuildBillSplitLineMemo(bill, split);

                var expenseLine = new JournalEntryLine
                {
                    // Debit Expense
                    ChartOfAccountId = expenseAccountId,
                    Debit = split.Amount,
                    Credit = 0,
                    Memo = splitMemo,
                    CreatedBy = currentUser
                };
                ApplyJournalEntryLineContext(expenseLine, CreateJournalEntryLineContextFromReceiptSplit(bill, split) with
                {
                    PropertyCode = defaultLineContext.PropertyCode,
                    ContactName = defaultLineContext.ContactName
                });
                journalEntryLines.Add(expenseLine);
            }
        }

        var negativeTotal = negativeSplits.Sum(s => s.Amount);
        if (negativeTotal < 0)
        {
            var accountsPayableLine = new JournalEntryLine
            {
                // Debit Accounts Payable
                ChartOfAccountId = accountsPayableAccountId,
                Debit = Math.Abs(negativeTotal),
                Credit = 0,
                Memo = billMemo,
                CreatedBy = currentUser
            };
            ApplyJournalEntryLineContext(accountsPayableLine, defaultLineContext);
            journalEntryLines.Add(accountsPayableLine);

            foreach (var split in negativeSplits)
            {
                var expenseAccountId = GetBillReceiptExpenseAccountId(split, bill.OfficeId, chartOfAccounts, accountingOffice);
                var splitMemo = BuildBillSplitLineMemo(bill, split);
                var expenseLine = new JournalEntryLine
                {
                    // Credit ExpenseAccountId
                    ChartOfAccountId = expenseAccountId,
                    Debit = 0,
                    Credit = Math.Abs(split.Amount),
                    Memo = splitMemo,
                    CreatedBy = currentUser
                };
                ApplyJournalEntryLineContext(expenseLine, CreateJournalEntryLineContextFromReceiptSplit(bill, split) with
                {
                    PropertyCode = defaultLineContext.PropertyCode,
                    ContactName = defaultLineContext.ContactName
                });
                journalEntryLines.Add(expenseLine);
            }
        }

        return new JournalEntry
        {
            OrganizationId = bill.OrganizationId,
            OfficeId = bill.OfficeId,
            TransactionDate = transactionDate,
            SourceTypeId = (int)SourceType.Bill,
            SourceId = bill.ReceiptId,
            SourceCode = ResolveJournalEntrySourceCodeFromReceipt(bill),
            Memo = billMemo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }

    private async Task<JournalEntry> BuildJournalEntryFromBillPaymentAsync(BillPaymentApplication paymentApplication, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // BILL-PAYMENT-JE-ACCOUNTS
        // Line 1 — Debit: Accounts Payable (GetDefaultAccountsPayable) or the credit card account on the bill's bank card.
        // Line 2 — Credit: Bank account selected on the payment (paymentApplication.ChartOfAccountId).
        // END BILL-PAYMENT-JE-ACCOUNTS

        var bill = paymentApplication.Bill;
        if (paymentApplication.AmountApplied == 0)
            throw new Exception("Bill payment amount cannot be zero");

        if (paymentApplication.PaymentDate == default)
            throw new Exception("Payment date is required to create a bill payment journal entry");

        if (paymentApplication.ChartOfAccountId <= 0)
            throw new Exception("Chart of account is required for bill payment");

        var payableOrCardAccountId = bill.BankCardId is > 0
            ? GetCreditCardAccountId(bill, chartOfAccounts, accountingOffice)
            : GetDefaultAccountsPayable(chartOfAccounts, bill.OfficeId, accountingOffice);
        var paymentBankAccountId = paymentApplication.ChartOfAccountId;

        var amount = paymentApplication.AmountApplied;
        var transactionDate = paymentApplication.PaymentDate;
        var billMemo = BuildBillJournalMemo(bill, bill.Splits);
        var lineContext = await ResolveReceiptJournalEntryLineContextAsync(bill);
        var payableLine = new JournalEntryLine
        {
            // Debit Accounts Payable or Credit Card
            ChartOfAccountId = payableOrCardAccountId,
            Debit = amount > 0 ? amount : 0,
            Credit = amount < 0 ? Math.Abs(amount) : 0,
            Memo = billMemo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(payableLine, lineContext);
        var paymentLine = new JournalEntryLine
        {
            // Credit Payment Bank Account
            ChartOfAccountId = paymentBankAccountId,
            Debit = amount < 0 ? Math.Abs(amount) : 0,
            Credit = amount > 0 ? amount : 0,
            Memo = billMemo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(paymentLine, lineContext);
        var journalEntryLines = new List<JournalEntryLine> { payableLine, paymentLine };

        return new JournalEntry
        {
            OrganizationId = bill.OrganizationId,
            OfficeId = bill.OfficeId,
            TransactionDate = transactionDate,
            SourceTypeId = (int)SourceType.BillPayment,
            SourceId = bill.ReceiptId,
            SourceCode = ResolveJournalEntrySourceCodeFromReceipt(bill),
            Memo = billMemo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        };
    }
