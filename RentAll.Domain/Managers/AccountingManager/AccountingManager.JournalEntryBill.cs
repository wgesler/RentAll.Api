    public async Task<JournalEntry?> CreateJournalEntryFromBillAsync(Receipt bill, Guid currentUser)
        => (await CreateJournalEntryFromBillWithResultAsync(bill, currentUser)).JournalEntry;

    // Inside function that returns loggable errors
    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromBillWithResultAsync(Receipt bill, Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(bill.OrganizationId))
            return AccountingJournalEntryResult.Success();

        try
        {
            // Only bills come here, receipts go another route
            EnsureReceiptIsBill(bill);
            bill = await LoadReceiptWithSplitsAsync(bill);

            // Create the main bill JE
            var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(bill.OrganizationId, bill.OfficeId);
            var journalEntry = await BuildJournalEntryFromBillAsync(bill, chartOfAccounts, accountingOffice, currentUser);
            var created = await CreateAutoGeneratedJournalEntryAsync(journalEntry);
            if (created != null)
                await PersistReceiptJournalEntryIdIfChangedAsync(bill, created.JournalEntryId, currentUser);

            try
            {
                // If that bill is a Utility, create a second JE for owner accountability
                var ownerUtilityJournalEntry = await BuildJournalEntryFromBillForOwnerUtilityAsync(bill, chartOfAccounts, accountingOffice, currentUser);
                if (ownerUtilityJournalEntry != null)
                {
                    await CreateAutoGeneratedJournalEntryAsync(ownerUtilityJournalEntry);
                }
            }
            catch (Exception ownerUtilityEx)
            {
                var ownerUtilityMessage = $"Owner utility journal entry failed for bill {(string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.ReceiptCode.Trim() : bill.BillNumber.Trim())}: {ownerUtilityEx.Message}";
                await LogAccountingErrorAsync(
                    trigger: "BillOwnerUtility",
                    organizationId: bill.OrganizationId,
                    officeId: bill.OfficeId,
                    sourceTypeId: (int)SourceType.Bill,
                    sourceId: bill.ReceiptId,
                    documentCode: string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.ReceiptCode.Trim() : bill.BillNumber.Trim(),
                    accountingPeriod: bill.AccountingPeriod == default ? null : bill.AccountingPeriod,
                    amount: bill.Amount,
                    message: ownerUtilityMessage,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(ownerUtilityMessage, created);
            }

            return AccountingJournalEntryResult.Success(created);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "Bill",
                organizationId: bill.OrganizationId,
                officeId: bill.OfficeId,
                sourceTypeId: (int)SourceType.Bill,
                sourceId: bill.ReceiptId,
                documentCode: string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.ReceiptCode.Trim() : bill.BillNumber.Trim(),
                accountingPeriod: bill.AccountingPeriod == default ? null : bill.AccountingPeriod,
                amount: bill.Amount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    public async Task<List<JournalEntry>> CreateJournalEntriesFromBillPaymentAsync(BillPayment billPayment, Guid currentUser)
    {
        var journalEntries = new List<JournalEntry>();

        if (billPayment.PaymentApplications.Count == 0)
            return journalEntries;

        foreach (var paymentApplication in billPayment.PaymentApplications)
        {
            var journalEntry = await CreateJournalEntryFromBillPaymentAsync(paymentApplication, currentUser);
            if (journalEntry != null)
                journalEntries.Add(journalEntry);
        }

        return journalEntries;
    }

    private async Task<JournalEntry?> CreateJournalEntryFromBillPaymentAsync(BillPaymentApplication paymentApplication, Guid currentUser)
        => (await CreateJournalEntryFromBillPaymentWithResultAsync(paymentApplication, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromBillPaymentWithResultAsync(BillPaymentApplication paymentApplication, Guid currentUser)
    {
        var bill = paymentApplication.Bill;
        if (bill == null)
            return AccountingJournalEntryResult.WarningResult("Bill is required to create a bill payment journal entry");

        if (!await IsAccountingFeatureEnabledAsync(bill.OrganizationId))
            return AccountingJournalEntryResult.Success();

        try
        {
            if (paymentApplication.PaymentSequence < 0)
                return AccountingJournalEntryResult.WarningResult("PaymentSequence is required to create a bill payment journal entry");

            if (bill.ReceiptId == Guid.Empty)
                return AccountingJournalEntryResult.WarningResult("ReceiptId is required to create a bill payment journal entry");

            var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(bill.OrganizationId, bill.OfficeId);
            var journalEntry = await BuildJournalEntryFromBillPaymentAsync(paymentApplication, chartOfAccounts, accountingOffice, currentUser);
            var created = await CreateAutoGeneratedJournalEntryAsync(journalEntry);
            return AccountingJournalEntryResult.Success(created);
        }
        catch (Exception ex)
        {
            var billLabel = !string.IsNullOrWhiteSpace(bill.BillNumber) ? bill.BillNumber.Trim() : bill.ReceiptCode.Trim();
            await LogAccountingErrorAsync(
                trigger: "BillPayment",
                organizationId: bill.OrganizationId,
                officeId: bill.OfficeId,
                sourceTypeId: (int)SourceType.BillPayment,
                sourceId: bill.ReceiptId,
                documentCode: billLabel,
                accountingPeriod: bill.AccountingPeriod == default ? null : bill.AccountingPeriod,
                amount: paymentApplication.AmountApplied,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }
