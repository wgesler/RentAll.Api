    private async Task UpsertOwnerActualJournalEntryForPaymentAsync(
        Invoice invoice,
        LedgerLine paymentLedgerLine,
        decimal paymentAmount,
        DateOnly transactionDate,
        List<JournalEntry> workingEntries,
        ISet<Guid> retainedEntryIds,
        Guid currentUser)
    {
        if (paymentAmount == 0 || paymentLedgerLine.LedgerLineId == Guid.Empty)
            return;

        try
        {
            // Amount context: Owner Expected lives on SourceType Invoice (Kind OwnerExpected).
            IReadOnlyList<JournalEntry> amountContextEntries = workingEntries;
            if (InvoiceCrossesAccountingPeriodBoundary(invoice))
            {
                var invoiceEntries = (await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
                {
                    OrganizationId = invoice.OrganizationId,
                    OfficeIds = invoice.OfficeId.ToString(),
                    SourceTypeId = (int)SourceType.Invoice,
                    SourceId = invoice.InvoiceId,
                    IncludeVoided = false,
                    IncludeUnposted = true
                })).ToList();
                amountContextEntries = invoiceEntries
                    .Concat(workingEntries)
                    .GroupBy(entry => entry.JournalEntryId)
                    .Select(group => group.First())
                    .ToList();
            }

            var journalEntry = await BuildJournalEntryFromPaymentForOwnerActualAsync(
                invoice,
                paymentLedgerLine,
                paymentAmount,
                transactionDate,
                currentUser,
                amountContextEntries);
            var existingOwnerActual = ClaimFirstMatchingEntry(
                workingEntries,
                entry => IsInvoiceOwnerActualPaymentJournalEntry(entry)
                    && MatchesJournalEntryAccountingPeriod(entry, invoice.AccountingPeriod));

            if (journalEntry == null || !journalEntry.JournalEntryLines.Any(line => line.Debit != 0 || line.Credit != 0))
                return;

            var updated = await UpsertAutoGeneratedJournalEntryAsync(
                journalEntry,
                existingOwnerActual != null ? [existingOwnerActual] : [],
                currentUser,
                invoice.OrganizationId);
            if (updated != null)
                retainedEntryIds.Add(updated.JournalEntryId);
            // Do not re-add to workingEntries: DeleteClaimedOrphanJournalEntriesAsync removes payment-flow
            // orphans (including OwnerActual) still listed there after upsert.
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "InvoiceOwnerActual",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.InvoicePayment,
                sourceId: paymentLedgerLine.LedgerLineId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: paymentAmount,
                message: ex.Message,
                currentUser: currentUser);
        }
    }

