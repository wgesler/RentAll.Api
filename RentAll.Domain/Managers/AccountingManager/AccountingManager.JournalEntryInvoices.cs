    private async Task LogInvoicePaymentSplitDecisionAsync(Invoice invoice, LedgerLine paymentLedgerLine, IReadOnlyList<InvoicePaymentSplitAllocation> splitAllocations, string? message = null)
    {
        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        var orderedAllocations = (splitAllocations ?? Array.Empty<InvoicePaymentSplitAllocation>())
            .OrderBy(a => a.AccountingPeriod)
            .ToList();
        var firstAllocation = orderedAllocations.ElementAtOrDefault(0);
        var secondAllocation = orderedAllocations.ElementAtOrDefault(1);
        var allocationSummary = string.Join(", ", orderedAllocations.Select(allocation => $"{allocation.AccountingPeriod:MM/yyyy}={allocation.Amount:0.00}"));
        var finalMessage = string.IsNullOrWhiteSpace(message)
            ? $"Payment split allocations: {allocationSummary}"
            : $"{message} {allocationSummary}";

        await LogAccountingLogAsync(new AccountingLog
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            PropertyId = propertyId,
            InvoiceId = invoice.InvoiceId,
            OriginalAmount = paymentLedgerLine.Amount,
            RentalLine = paymentLedgerLine.Description,
            Split = orderedAllocations.Count > 1,
            FirstPeriod = firstAllocation == null ? null : firstAllocation.AccountingPeriod.ToString("MM/yyyy"),
            SecondPeriod = secondAllocation == null ? null : secondAllocation.AccountingPeriod.ToString("MM/yyyy"),
            FirstAmount = firstAllocation?.Amount,
            SecondAmount = secondAllocation?.Amount,
            Message = finalMessage
        });
    }
