    private async Task<JournalEntry> CreateJournalEntryFromInvoiceAsync(Invoice invoice, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser, Guid? sourceIdOverride = null)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // INVOICE-JE-ACCOUNTS
        //   Line 1   — Debit: Accounts Receivable (GetDefaultAccountsReceivable) for the invoice total.
        //   Lines 2+ — Credit: Tenant Income (GetDefaultTenantIncome) for each non-payment charge line.
        // END INVOICE-JE-ACCOUNTS

        var costCodeById = await LoadCostCodeByOfficeIdAsync(invoice.OrganizationId, invoice.OfficeId);
        var accountsReceivableAccountId = GetDefaultAccountsReceivable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var lineContext = await ResolveInvoiceJournalEntryLineContextAsync(invoice);

        var chargeLines = invoice.LedgerLines.Where(l => l.Amount != 0).Where(l =>
        {
            costCodeById.TryGetValue(l.CostCodeId, out var costCode);
            return !IsPaymentLedgerLine(costCode);
        }).OrderBy(l => l.LineNumber).ToList();

        var totalAmount = chargeLines.Sum(l => l.Amount);
        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create a journal entry for an invoice");

        var transactionDate = invoice.AccountingPeriod;
        var invoiceMemo = BuildInvoiceJournalEntryMemo(invoice, chargeLines);
        var accountsReceivableLine = new JournalEntryLine
        {
            // Debit Accounts Recievable
            ChartOfAccountId = accountsReceivableAccountId,
            Debit = totalAmount > 0 ? totalAmount : 0,
            Credit = totalAmount < 0 ? Math.Abs(totalAmount) : 0,
            Memo = BuildAccountsReceivableMemo(invoice.InvoiceCode),
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(accountsReceivableLine, lineContext);
        var journalEntryLines = new List<JournalEntryLine> { accountsReceivableLine };

        foreach (var line in chargeLines)
        {
            costCodeById.TryGetValue(line.CostCodeId, out var costCode);
            var chargeLine = new JournalEntryLine
            {
                // Debit Tenant Income
                ChartOfAccountId = GetDefaultTenantIncome(chartOfAccounts, invoice.OfficeId, accountingOffice, costCode),
                CostCodeId = line.CostCodeId,
                Debit = line.Amount < 0 ? Math.Abs(line.Amount) : 0,
                Credit = line.Amount > 0 ? line.Amount : 0,
                Memo = BuildInvoiceChargeLineMemo(invoice, line),
                CreatedBy = currentUser
            };
            var chargeLineContext = lineContext with
            {
                ReservationId = NormalizeOptionalGuid(line.ReservationId) ?? lineContext.ReservationId
            };
            ApplyJournalEntryLineContext(chargeLine, chargeLineContext);
            journalEntryLines.Add(chargeLine);
        }

        return ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = transactionDate,
            AccountingPeriod = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = sourceIdOverride ?? invoice.InvoiceId,
            SourceCode = ResolveJournalEntrySourceCodeFromInvoice(invoice),
            Memo = invoiceMemo,
            JournalEntryLines = journalEntryLines,
            CreatedBy = currentUser
        }, JournalEntryKind.Charge, Perspective.Company);
    }

    private async Task<JournalEntry> CreateJournalEntryFromPaymentAsync(Invoice invoice, LedgerLine paymentLedgerLine, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        // AGENT-NOTE: DO NOT TOUCH.
        // PAYMENT-JE-ACCOUNTS
        // Line 1 — Debit: Undeposited Funds (GetDefaultUndepositedFunds).
        // Line 2 — Credit: Accounts Receivable (GetDefaultAccountsReceivable).
        // END PAYMENT-JE-ACCOUNTS

        if (paymentLedgerLine.Amount == 0)
            throw new Exception("Payment amount cannot be zero");

        if (paymentLedgerLine.LedgerLineDate == default)
            throw new Exception("Payment date is required to create a payment journal entry");

        var costCodeById = await LoadCostCodeByOfficeIdAsync(invoice.OrganizationId, invoice.OfficeId);
        if (!costCodeById.TryGetValue(paymentLedgerLine.CostCodeId, out var paymentCostCode) || !IsPaymentLedgerLine(paymentCostCode))
            throw new Exception("Payment ledger line must use a payment cost code");

        var accountsReceivableAccountId = GetDefaultAccountsReceivable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var undepositedFundsAccountId = GetDefaultUndepositedFunds(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var lineContext = await ResolveInvoiceJournalEntryLineContextAsync(invoice, paymentLedgerLine);

        var amount = paymentLedgerLine.Amount;
        var transactionDate = ResolveInvoicePaymentJournalEntryDate(paymentLedgerLine);
        var memo = BuildInvoicePaymentMemo(invoice.InvoiceCode, paymentLedgerLine.Description);

        var undepositedFundsLine = new JournalEntryLine
        {
            ChartOfAccountId = undepositedFundsAccountId,
            CostCodeId = paymentLedgerLine.CostCodeId,
            Debit = amount > 0 ? amount : 0,
            Credit = amount < 0 ? Math.Abs(amount) : 0,
            Memo = memo,
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(undepositedFundsLine, lineContext);
        var accountsReceivableLine = new JournalEntryLine
        {
            ChartOfAccountId = accountsReceivableAccountId,
            Debit = amount < 0 ? Math.Abs(amount) : 0,
            Credit = amount > 0 ? amount : 0,
            Memo = BuildAccountsReceivableMemo(invoice.InvoiceCode),
            CreatedBy = currentUser
        };
        ApplyJournalEntryLineContext(accountsReceivableLine, lineContext);

        return ClassifyJournalEntry(new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = transactionDate,
            AccountingPeriod = invoice.AccountingPeriod != default
                ? invoice.AccountingPeriod
                : FirstDayOfMonth(transactionDate),
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            SourceCode = ResolveJournalEntrySourceCodeFromInvoice(invoice),
            Memo = memo,
            JournalEntryLines = new List<JournalEntryLine> { undepositedFundsLine, accountsReceivableLine },
            CreatedBy = currentUser
        }, JournalEntryKind.Payment, Perspective.Company);
    }

