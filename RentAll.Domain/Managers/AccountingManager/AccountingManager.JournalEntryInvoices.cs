    public async Task<JournalEntry?> CreateJournalEntryFromInvoiceAsync(Invoice invoice, Guid currentUser)
        => (await CreateJournalEntryFromInvoiceWithResultAsync(invoice, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromInvoiceWithResultAsync(Invoice invoice, Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(invoice.OrganizationId))
            return AccountingJournalEntryResult.Success();

        try
        {
            return await CreateJournalEntryFromInvoiceCoreAsync(invoice, currentUser);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "Invoice",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.Invoice,
                sourceId: invoice.InvoiceId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: invoice.TotalAmount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromInvoiceCoreAsync(Invoice invoice, Guid currentUser)
    {
        if (await TryUseCrossPeriodInvoiceJournalEntryPathAsync(invoice))
        {
            var (crossPeriodEntry, decisionMessage, postAsStandardInvoice) = await CreateJournalEntriesFromCrossPeriodInvoiceAsync(invoice, currentUser);
            if (crossPeriodEntry != null)
            {
                return AccountingJournalEntryResult.Success(crossPeriodEntry);
            }

            await LogInvoiceSplitDecisionAsync(invoice, split: false, message: decisionMessage);

            if (!postAsStandardInvoice)
            {
                var message = $"Invoice {invoice.InvoiceCode} spans two accounting periods but the split failed: {decisionMessage}";
                await LogAccountingErrorAsync(
                    trigger: "Invoice",
                    organizationId: invoice.OrganizationId,
                    officeId: invoice.OfficeId,
                    sourceTypeId: (int)SourceType.Invoice,
                    sourceId: invoice.InvoiceId,
                    documentCode: invoice.InvoiceCode,
                    accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                    amount: invoice.TotalAmount,
                    message: message,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(message);
            }
        }

        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var journalEntry = await CreateJournalEntryFromInvoiceAsync(invoice, chartOfAccounts, accountingOffice, currentUser);
        var createdJournalEntry = await CreateAutoGeneratedJournalEntryAsync(journalEntry);

        if (TryGetInvoiceRentalLineAmount(invoice, out _))
        {
            try
            {
                var rentPlus4000Base = await GetInvoiceRentPlus4000BaseAsync(invoice);
                await CreateJournalEntryFromInvoiceForOwnerShareAsync(invoice, rentPlus4000Base, currentUser);
            }
            catch (Exception ex)
            {
                var ownerMessage = $"Owner share journal entry failed for invoice {invoice.InvoiceCode}: {ex.Message}";
                await LogAccountingErrorAsync(
                    trigger: "InvoiceOwnerShare",
                    organizationId: invoice.OrganizationId,
                    officeId: invoice.OfficeId,
                    sourceTypeId: (int)SourceType.Invoice,
                    sourceId: invoice.InvoiceId,
                    documentCode: invoice.InvoiceCode,
                    accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                    amount: invoice.TotalAmount,
                    message: ownerMessage,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(ownerMessage, createdJournalEntry);
            }
        }

        return AccountingJournalEntryResult.Success(createdJournalEntry);
    }

    public async Task<JournalEntry?> CreateJournalEntryFromPaymentAsync(Invoice invoice, LedgerLine paymentLedgerLine, Guid currentUser)
        => (await CreateJournalEntryFromPaymentWithResultAsync(invoice, paymentLedgerLine, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromPaymentWithResultAsync(Invoice invoice, LedgerLine paymentLedgerLine, Guid currentUser)
    {
        var existingPaymentEntries = await GetJournalEntriesForInvoicePaymentLedgerLineAsync(
            invoice.OrganizationId,
            invoice.OfficeId,
            invoice,
            paymentLedgerLine);
        return await UpsertJournalEntryFromPaymentAsync(invoice, paymentLedgerLine, existingPaymentEntries, currentUser);
    }

    private async Task<AccountingJournalEntryResult> UpsertJournalEntryFromPaymentAsync(
        Invoice invoice,
        LedgerLine paymentLedgerLine,
        IReadOnlyList<JournalEntry> existingPaymentEntries,
        Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(invoice.OrganizationId))
            return AccountingJournalEntryResult.Success();

        var retainedEntryIds = new HashSet<Guid>();
        var workingEntries = existingPaymentEntries.ToList();

        try
        {
            if (paymentLedgerLine.LedgerLineId == Guid.Empty)
                return AccountingJournalEntryResult.WarningResult("LedgerLineId is required to create a payment journal entry");

            var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
            var prePaymentAccountId = GetDefaultPrePayment(chartOfAccounts, invoice.OfficeId, accountingOffice);
            var splitAllocations = await GetInvoicePaymentSplitAllocationsAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice);

            if (splitAllocations.Count > 0)
            {
                // Cross-period payment: recognize cash OwnAct (first split) and PrePay receive (second split) together.
                // PrePay apply + second OwnAct happen in the apply step below.
                var paymentJournalEntry = await CreateJournalEntryFromPaymentAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
                var updatedPayment = await UpsertClaimedAutoGeneratedJournalEntryAsync(
                    paymentJournalEntry,
                    workingEntries,
                    entry => IsStandardInvoicePaymentJournalEntry(entry),
                    retainedEntryIds,
                    currentUser,
                    invoice.OrganizationId);

                foreach (var splitAllocation in splitAllocations)
                {
                    var splitInvoice = CloneInvoiceForPaymentAccountingPeriod(invoice, splitAllocation.AccountingPeriod);
                    var splitPaymentLedgerLine = ClonePaymentLedgerLineWithAmount(paymentLedgerLine, splitAllocation.Amount);
                    if (IsInvoicePrePayment(splitInvoice, splitPaymentLedgerLine))
                    {
                        var receivedResult = await UpsertPrePaymentReceivedJournalEntryAsync(
                            splitInvoice,
                            splitPaymentLedgerLine,
                            chartOfAccounts,
                            accountingOffice,
                            workingEntries,
                            prePaymentAccountId,
                            retainedEntryIds,
                            currentUser);
                        if (receivedResult.HasWarning)
                            return AccountingJournalEntryResult.WarningResult(receivedResult.Warning!, updatedPayment);
                    }
                    else
                    {
                        await UpsertOwnerActualJournalEntryForPaymentAsync(
                            splitInvoice,
                            splitPaymentLedgerLine,
                            splitAllocation.Amount,
                            ResolveInvoicePaymentJournalEntryDate(splitPaymentLedgerLine),
                            workingEntries,
                            retainedEntryIds,
                            currentUser);
                    }
                }

                var allocatedAmount = splitAllocations.Sum(allocation => allocation.Amount);
                var unappliedAmount = paymentLedgerLine.Amount - allocatedAmount;
                if (unappliedAmount > 0)
                {
                    var unappliedLedgerLine = ClonePaymentLedgerLineWithAmount(paymentLedgerLine, unappliedAmount);
                    var unappliedPrePaymentResult = await UpsertPrePaymentReceivedJournalEntryAsync(
                        invoice,
                        unappliedLedgerLine,
                        chartOfAccounts,
                        accountingOffice,
                        workingEntries,
                        prePaymentAccountId,
                        retainedEntryIds,
                        currentUser);
                    if (unappliedPrePaymentResult.HasWarning)
                        return AccountingJournalEntryResult.WarningResult(unappliedPrePaymentResult.Warning!, updatedPayment);
                }

                await LogInvoicePaymentSplitDecisionAsync(invoice, paymentLedgerLine, splitAllocations, "Cross-period payment split applied.");

                foreach (var splitAllocation in splitAllocations)
                {
                    var splitInvoice = CloneInvoiceForPaymentAccountingPeriod(invoice, splitAllocation.AccountingPeriod);
                    var splitPaymentLedgerLine = ClonePaymentLedgerLineWithAmount(paymentLedgerLine, splitAllocation.Amount);
                    if (!IsInvoicePrePayment(splitInvoice, splitPaymentLedgerLine))
                        continue;

                    // Apply PrePay for the second split, then post OwnAct for that period.
                    var applyResult = await UpsertPrePaymentApplyJournalEntryAsync(
                        splitInvoice,
                        splitPaymentLedgerLine,
                        chartOfAccounts,
                        accountingOffice,
                        workingEntries,
                        prePaymentAccountId,
                        retainedEntryIds,
                        currentUser);
                    if (applyResult.HasWarning)
                        return AccountingJournalEntryResult.WarningResult(applyResult.Warning!, updatedPayment);
                }

                await DeleteClaimedOrphanJournalEntriesAsync(workingEntries, invoice.OrganizationId);
                return AccountingJournalEntryResult.Success(updatedPayment);
            }

            var journalEntry = await CreateJournalEntryFromPaymentAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
            var updatedJournalEntry = await UpsertClaimedAutoGeneratedJournalEntryAsync(
                journalEntry,
                workingEntries,
                entry => IsStandardInvoicePaymentJournalEntry(entry),
                retainedEntryIds,
                currentUser,
                invoice.OrganizationId);

            if (IsInvoicePrePayment(invoice, paymentLedgerLine))
            {
                var prePaymentResult = await UpsertJournalEntriesFromPrePaymentAsync(
                    invoice,
                    paymentLedgerLine,
                    chartOfAccounts,
                    accountingOffice,
                    workingEntries,
                    retainedEntryIds,
                    currentUser);
                await DeleteClaimedOrphanJournalEntriesAsync(workingEntries, invoice.OrganizationId);
                if (prePaymentResult.HasWarning)
                    return prePaymentResult;

                return AccountingJournalEntryResult.Success(prePaymentResult.JournalEntry ?? updatedJournalEntry);
            }

            await UpsertOwnerActualJournalEntryForPaymentAsync(
                invoice,
                paymentLedgerLine,
                paymentLedgerLine.Amount,
                ResolveInvoicePaymentJournalEntryDate(paymentLedgerLine),
                workingEntries,
                retainedEntryIds,
                currentUser);

            await DeleteClaimedOrphanJournalEntriesAsync(workingEntries, invoice.OrganizationId);
            return AccountingJournalEntryResult.Success(updatedJournalEntry);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "InvoicePayment",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.InvoicePayment,
                sourceId: paymentLedgerLine.LedgerLineId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: paymentLedgerLine.Amount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    private async Task<AccountingJournalEntryResult> UpsertJournalEntriesFromPrePaymentAsync(
        Invoice invoice,
        LedgerLine paymentLedgerLine,
        List<ChartOfAccount> chartOfAccounts,
        AccountingOffice? accountingOffice,
        List<JournalEntry> workingEntries,
        ISet<Guid> retainedEntryIds,
        Guid currentUser)
    {
        var receivedResult = await UpsertPrePaymentReceivedJournalEntryAsync(
            invoice,
            paymentLedgerLine,
            chartOfAccounts,
            accountingOffice,
            workingEntries,
            GetDefaultPrePayment(chartOfAccounts, invoice.OfficeId, accountingOffice),
            retainedEntryIds,
            currentUser);
        if (receivedResult.HasWarning)
            return receivedResult;

        if (receivedResult.JournalEntry == null)
            return AccountingJournalEntryResult.Success();

        var applyResult = await UpsertPrePaymentApplyJournalEntryAsync(
            invoice,
            paymentLedgerLine,
            chartOfAccounts,
            accountingOffice,
            workingEntries,
            GetDefaultPrePayment(chartOfAccounts, invoice.OfficeId, accountingOffice),
            retainedEntryIds,
            currentUser);
        if (applyResult.HasWarning)
            return applyResult;

        return AccountingJournalEntryResult.Success(receivedResult.JournalEntry);
    }

