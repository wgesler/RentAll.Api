    private async Task<List<InvoicePaymentSplitAllocation>> GetInvoicePaymentSplitAllocationsAsync(Invoice invoice, LedgerLine paymentLedgerLine, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice)
    {
        var paymentAmount = paymentLedgerLine.Amount;
        if (paymentAmount == 0)
            return new List<InvoicePaymentSplitAllocation>();

        var accountsReceivableAccountId = GetDefaultAccountsReceivable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var sourceJournalEntries = (await _journalEntryRepository.GetJournalEntriesAsync(new JournalEntryGetCriteria
        {
            OrganizationId = invoice.OrganizationId,
            OfficeIds = invoice.OfficeId.ToString(),
            SourceTypeId = (int)SourceType.Invoice,
            SourceId = invoice.InvoiceId,
            IncludeVoided = false,
            IncludeUnposted = true
        })).ToList();
        if (sourceJournalEntries.Count == 0)
            return new List<InvoicePaymentSplitAllocation>();

        var arAmountsByPeriod = sourceJournalEntries
            .Select(entry => new
            {
                entry.TransactionDate,
                ArAmount = entry.JournalEntryLines.Where(line => line.ChartOfAccountId == accountsReceivableAccountId).Sum(line => line.Debit - line.Credit)
            })
            .Where(item => item.ArAmount != 0)
            .GroupBy(item => item.TransactionDate)
            .Select(group => new { AccountingPeriod = group.Key, ArAmount = group.Sum(item => item.ArAmount) })
            .Where(item => item.ArAmount != 0)
            .OrderBy(item => item.AccountingPeriod)
            .ToList();

        if (arAmountsByPeriod.Count <= 1)
            return new List<InvoicePaymentSplitAllocation>();

        var totalAbsoluteArAmount = arAmountsByPeriod.Sum(item => Math.Abs(item.ArAmount));
        if (totalAbsoluteArAmount == 0)
            return new List<InvoicePaymentSplitAllocation>();

        var splitAllocations = new List<InvoicePaymentSplitAllocation>();
        var allocatedAmount = 0m;
        for (var index = 0; index < arAmountsByPeriod.Count; index++)
        {
            var periodArAmount = arAmountsByPeriod[index];
            var isLastPeriod = index == arAmountsByPeriod.Count - 1;
            var sliceAmount = isLastPeriod
                ? paymentAmount - allocatedAmount
                : Math.Round(paymentAmount * (Math.Abs(periodArAmount.ArAmount) / totalAbsoluteArAmount), 2, MidpointRounding.AwayFromZero);
            allocatedAmount += sliceAmount;
            if (sliceAmount == 0)
                continue;

            splitAllocations.Add(new InvoicePaymentSplitAllocation { AccountingPeriod = periodArAmount.AccountingPeriod, Amount = sliceAmount });
        }

        return splitAllocations.Count > 1 ? splitAllocations : new List<InvoicePaymentSplitAllocation>();
    }

