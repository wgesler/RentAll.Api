    public async Task<JournalEntry?> CreateJournalEntryFromInvoiceAsync(Invoice invoice, Guid currentUser)
        => (await CreateJournalEntryFromInvoiceWithResultAsync(invoice, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromInvoiceWithResultAsync(Invoice invoice, Guid currentUser)
    {
        if (!await IsAccountingFeatureEnabledAsync(invoice.OrganizationId))
            return AccountingJournalEntryResult.Success();

        try
        {
            return await CreateJournalEntryFromInvoiceCoreAsync(invoice, currentUser);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "Invoice",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.Invoice,
                sourceId: invoice.InvoiceId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: invoice.TotalAmount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromInvoiceCoreAsync(Invoice invoice, Guid currentUser)
    {
        if (await TryUseCrossPeriodInvoiceJournalEntryPathAsync(invoice))
        {
            var (crossPeriodEntry, decisionMessage, postAsStandardInvoice) = await CreateJournalEntriesFromCrossPeriodInvoiceAsync(invoice, currentUser);
            if (crossPeriodEntry != null)
                return AccountingJournalEntryResult.Success(crossPeriodEntry);

            await LogInvoiceSplitDecisionAsync(invoice, split: false, message: decisionMessage);

            if (!postAsStandardInvoice)
            {
                var message = $"Invoice {invoice.InvoiceCode} spans two accounting periods but the split failed: {decisionMessage}";
                await LogAccountingErrorAsync(
                    trigger: "Invoice",
                    organizationId: invoice.OrganizationId,
                    officeId: invoice.OfficeId,
                    sourceTypeId: (int)SourceType.Invoice,
                    sourceId: invoice.InvoiceId,
                    documentCode: invoice.InvoiceCode,
                    accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                    amount: invoice.TotalAmount,
                    message: message,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(message);
            }
        }

        var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
        var journalEntry = await BuildJournalEntryFromInvoiceAsync(invoice, chartOfAccounts, accountingOffice, currentUser);
        var createdJournalEntry = await CreateAutoGeneratedJournalEntryAsync(journalEntry);

        if (TryGetInvoiceRentalLineAmount(invoice, out _))
        {
            try
            {
                var ownerBase = await GetOwnerPercentageBaseAsync(invoice);
                await CreateJournalEntryFromInvoiceOwnerShareAsync(invoice, ownerBase, currentUser);
            }
            catch (Exception ex)
            {
                var ownerMessage = $"Owner share journal entry failed for invoice {invoice.InvoiceCode}: {ex.Message}";
                await LogAccountingErrorAsync(
                    trigger: "InvoiceOwnerShare",
                    organizationId: invoice.OrganizationId,
                    officeId: invoice.OfficeId,
                    sourceTypeId: (int)SourceType.Invoice,
                    sourceId: invoice.InvoiceId,
                    documentCode: invoice.InvoiceCode,
                    accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                    amount: invoice.TotalAmount,
                    message: ownerMessage,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(ownerMessage, createdJournalEntry);
            }
        }

        return AccountingJournalEntryResult.Success(createdJournalEntry);
    }

    public async Task<JournalEntry?> CreateJournalEntryFromPaymentAsync(Invoice invoice, LedgerLine paymentLedgerLine, Guid currentUser)
        => (await CreateJournalEntryFromPaymentWithResultAsync(invoice, paymentLedgerLine, currentUser)).JournalEntry;

    private async Task<AccountingJournalEntryResult> CreateJournalEntryFromPaymentWithResultAsync(Invoice invoice, LedgerLine paymentLedgerLine, Guid currentUser, bool skipCrossPeriodSplit = false)
    {
        if (!await IsAccountingFeatureEnabledAsync(invoice.OrganizationId))
            return AccountingJournalEntryResult.Success();

        try
        {
            if (paymentLedgerLine.LedgerLineId == Guid.Empty)
                return AccountingJournalEntryResult.WarningResult("LedgerLineId is required to create a payment journal entry");

            var (chartOfAccounts, accountingOffice) = await LoadAccountContextAsync(invoice.OrganizationId, invoice.OfficeId);
            if (!skipCrossPeriodSplit)
            {
                var splitAllocations = await GetInvoicePaymentSplitAllocationsAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice);
                if (splitAllocations.Count > 1)
                {
                    JournalEntry? firstSplitJournalEntry = null;
                    foreach (var splitAllocation in splitAllocations)
                    {
                        var splitInvoice = CloneInvoiceForPaymentAccountingPeriod(invoice, splitAllocation.AccountingPeriod);
                        var splitPaymentLedgerLine = ClonePaymentLedgerLineWithAmount(paymentLedgerLine, splitAllocation.Amount);
                        var splitResult = await CreateJournalEntryFromPaymentWithResultAsync(splitInvoice, splitPaymentLedgerLine, currentUser, true);
                        if (splitResult.HasWarning)
                            return AccountingJournalEntryResult.WarningResult(splitResult.Warning!, firstSplitJournalEntry ?? splitResult.JournalEntry);

                        if (firstSplitJournalEntry == null && splitResult.JournalEntry != null)
                            firstSplitJournalEntry = splitResult.JournalEntry;
                    }

                    await LogInvoicePaymentSplitDecisionAsync(invoice, paymentLedgerLine, splitAllocations, "Cross-period payment split applied.");
                    return AccountingJournalEntryResult.Success(firstSplitJournalEntry);
                }
            }

            var journalEntry = await BuildJournalEntryFromPaymentAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
            await CreateAutoGeneratedJournalEntryAsync(journalEntry);

            try
            {
                var ownerPaymentJournalEntry = await BuildOwnerPaymentJournalEntryAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
                if (ownerPaymentJournalEntry != null)
                    await CreateAutoGeneratedJournalEntryAsync(ownerPaymentJournalEntry);
            }
            catch (Exception ex)
            {
                var ownerPaymentMessage = $"Owner payment journal entry failed for invoice {invoice.InvoiceCode}: {ex.Message}";
                await LogAccountingErrorAsync(
                    trigger: "InvoicePaymentOwnerShare",
                    organizationId: invoice.OrganizationId,
                    officeId: invoice.OfficeId,
                    sourceTypeId: (int)SourceType.InvoicePayment,
                    sourceId: paymentLedgerLine.LedgerLineId,
                    documentCode: invoice.InvoiceCode,
                    accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                    amount: paymentLedgerLine.Amount,
                    message: ownerPaymentMessage,
                    currentUser: currentUser);
                return AccountingJournalEntryResult.WarningResult(ownerPaymentMessage, journalEntry);
            }

            if (IsInvoicePrePayment(invoice, paymentLedgerLine))
            {
                var prePaymentResult = await CreateJournalEntriesFromPrePaymentWithResultAsync(invoice, paymentLedgerLine, chartOfAccounts, accountingOffice, currentUser);
                if (prePaymentResult.HasWarning)
                    return prePaymentResult;

                return AccountingJournalEntryResult.Success(prePaymentResult.JournalEntry ?? journalEntry);
            }

            return AccountingJournalEntryResult.Success(journalEntry);
        }
        catch (Exception ex)
        {
            await LogAccountingErrorAsync(
                trigger: "InvoicePayment",
                organizationId: invoice.OrganizationId,
                officeId: invoice.OfficeId,
                sourceTypeId: (int)SourceType.InvoicePayment,
                sourceId: paymentLedgerLine.LedgerLineId,
                documentCode: invoice.InvoiceCode,
                accountingPeriod: invoice.AccountingPeriod == default ? null : invoice.AccountingPeriod,
                amount: paymentLedgerLine.Amount,
                message: ex.Message,
                currentUser: currentUser);
            return AccountingJournalEntryResult.WarningResult(ex.Message);
        }
    }

