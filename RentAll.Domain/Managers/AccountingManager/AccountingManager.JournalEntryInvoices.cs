    private async Task<JournalEntry?> BuildOwnerPaymentJournalEntryAsync(Invoice invoice, LedgerLine paymentLedgerLine, List<ChartOfAccount> chartOfAccounts, AccountingOffice? accountingOffice, Guid currentUser)
    {
        if (paymentLedgerLine.Amount == 0)
            return null;

        if (!TryGetInvoiceRentalLineAmount(invoice, out _))
            return null;

        if (invoice.TotalAmount == 0)
            return null;

        var ownerBase = await GetOwnerPercentageBaseAsync(invoice);
        var ownerShareAmount = await CalculateOwnerShareAmountForInvoiceAsync(invoice, ownerBase);
        if (!ownerShareAmount.HasValue || ownerShareAmount.Value == 0)
            return null;

        var ownerPaymentAmount = Math.Round(ownerShareAmount.Value * (paymentLedgerLine.Amount / invoice.TotalAmount), 2, MidpointRounding.AwayFromZero);
        if (ownerPaymentAmount == 0)
            return null;

        var propertyId = await ResolveInvoicePropertyIdAsync(invoice);
        if (!propertyId.HasValue || propertyId == Guid.Empty)
            return null;

        var property = await _propertyRepository.GetPropertyByIdAsync(propertyId.Value, invoice.OrganizationId);
        if (property?.Owner1Id is not { } ownerContactId || ownerContactId == Guid.Empty)
            return null;

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create an owner payment journal entry");

        var undepositedFundsAccountId = GetDefaultUndepositedFunds(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var ownerAccountsPayableAccountId = GetDefaultOwnerAccountsPayable(chartOfAccounts, invoice.OfficeId, accountingOffice);
        var reservationId = paymentLedgerLine.ReservationId ?? invoice.ReservationId;
        var memo = BuildOwnerInvoicePaymentMemo(invoice.InvoiceCode);

        return new JournalEntry
        {
            OrganizationId = invoice.OrganizationId,
            OfficeId = invoice.OfficeId,
            TransactionDate = invoice.AccountingPeriod,
            PostingDate = invoice.AccountingPeriod,
            SourceTypeId = (int)SourceType.InvoicePayment,
            SourceId = paymentLedgerLine.LedgerLineId,
            Memo = memo,
            JournalEntryLines = new List<JournalEntryLine>
            {
                new()
                {
                    ChartOfAccountId = undepositedFundsAccountId,
                    CostCodeId = paymentLedgerLine.CostCodeId,
                    ReservationId = reservationId,
                    PropertyId = propertyId,
                    ContactId = ownerContactId,
                    Debit = ownerPaymentAmount > 0 ? ownerPaymentAmount : 0,
                    Credit = ownerPaymentAmount < 0 ? Math.Abs(ownerPaymentAmount) : 0,
                    Memo = memo,
                    CreatedBy = currentUser
                },
                new()
                {
                    ChartOfAccountId = ownerAccountsPayableAccountId,
                    ReservationId = reservationId,
                    PropertyId = propertyId,
                    ContactId = ownerContactId,
                    Debit = ownerPaymentAmount < 0 ? Math.Abs(ownerPaymentAmount) : 0,
                    Credit = ownerPaymentAmount > 0 ? ownerPaymentAmount : 0,
                    Memo = memo,
                    CreatedBy = currentUser
                }
            },
            CreatedBy = currentUser
        };
    }

    private async Task ValidatePrePaymentLedgerLineForJournalEntryAsync(Invoice invoice, LedgerLine paymentLedgerLine)
    {
        if (paymentLedgerLine.Amount == 0)
            throw new Exception("Payment amount cannot be zero");

        if (paymentLedgerLine.LedgerLineDate == default)
            throw new Exception("Payment date is required to create a payment journal entry");

        if (invoice.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required to create pre-payment journal entries");

        var costCodeById = await LoadCostCodeByOfficeIdAsync(invoice.OrganizationId, invoice.OfficeId);
        if (!costCodeById.TryGetValue(paymentLedgerLine.CostCodeId, out var paymentCostCode) || !IsPaymentLedgerLine(paymentCostCode))
            throw new Exception("Payment ledger line must use a payment cost code");
    }

    private async Task DeleteJournalEntriesForInvoicePaymentLedgerLineAsync(Guid organizationId, int officeId, Guid ledgerLineId)
    {
        await DeleteJournalEntriesForSourceAsync(organizationId, officeId, (int)SourceType.InvoicePayment, ledgerLineId);
        await DeleteJournalEntriesForSourceAsync(organizationId, officeId, (int)SourceType.Invoice, ledgerLineId);
    }

    private async Task<Guid?> ResolveInvoicePropertyIdAsync(Invoice invoice)
    {
        if (invoice.PropertyId.HasValue && invoice.PropertyId != Guid.Empty)
            return invoice.PropertyId;

        if (!invoice.ReservationId.HasValue || invoice.ReservationId == Guid.Empty)
            return null;

        if (invoice.ReservationId == SystemOrganization)
            return null;

        var reservation = await _reservationRepository.GetReservationByIdAsync(invoice.ReservationId.Value, invoice.OrganizationId);
        if (reservation == null || reservation.PropertyId == Guid.Empty)
            return null;

        return reservation.PropertyId;
    }
