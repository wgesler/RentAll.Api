        await DeleteJournalEntriesForSourceAsync(transfer.OrganizationId, transfer.OfficeId, (int)SourceType.Transfer, transfer.TransferId);
    }

    private async Task DeleteJournalEntriesForPaymentAsync(Payment payment)
    {
        if (payment.PaymentId == Guid.Empty)
            return;

        var entries = await GetJournalEntriesForSourceAsync(
            payment.OrganizationId,
            payment.OfficeId,
            SourceType.InvoicePayment,
            payment.PaymentId);

        foreach (var entry in entries)
            await DeleteOpenJournalEntryAsync(entry.JournalEntryId, payment.OrganizationId);
    }

    public async Task<JournalEntry> UnpostJournalEntryAsync(Guid journalEntryId, Guid organizationId, Guid currentUser)
    {
        var journalEntry = await _journalEntryRepository.GetJournalEntryByIdAsync(journalEntryId, organizationId);
        if (journalEntry == null)
            throw new Exception("Journal entry not found");

        if (journalEntry.PostingStatusId is PostingStatus.SoftClosed or PostingStatus.HardClosed)
            throw new Exception("A voided journal entry cannot be unposted");

        if (journalEntry.PostingStatusId != PostingStatus.Posted)
            throw new Exception("Journal entry is not posted");

        journalEntry.PostingStatusId = PostingStatus.Open;
        journalEntry.ModifiedBy = currentUser;
        return await _journalEntryRepository.UpdateJournalEntryByIdAsync(journalEntry);
    }

    private static bool HasActiveJournalEntryLines(JournalEntry journalEntry)
        => journalEntry.JournalEntryLines.Any(l => l.Debit != 0 || l.Credit != 0);

    private static bool HasInvalidActiveJournalEntryAccountIds(JournalEntry journalEntry)
        => journalEntry.JournalEntryLines.Any(l => (l.Debit != 0 || l.Credit != 0) && l.ChartOfAccountId <= 0);

    private static void ValidateJournalEntryForSave(JournalEntry journalEntry, bool requireActiveLines)
    {
        if (journalEntry.OfficeId <= 0)
            throw new Exception("OfficeId is required");

        if (journalEntry.TransactionDate == default)
            throw new Exception("TransactionDate is required");

        if (journalEntry.AccountingPeriod == default)
            throw new Exception("AccountingPeriod is required");

        var activeLines = journalEntry.JournalEntryLines
            .Where(l => l.Debit != 0 || l.Credit != 0)
            .ToList();

        if (requireActiveLines && activeLines.Count == 0)
            throw new Exception("At least one journal entry line is required");

        foreach (var line in activeLines)
        {
            if (line.ChartOfAccountId <= 0)
                throw new Exception("ChartOfAccountId is required on each journal entry line");

            if (line.Debit < 0 || line.Credit < 0)
                throw new Exception("Debit and Credit must be zero or greater");

            if (line.Debit != 0 && line.Credit != 0)
                throw new Exception("A journal entry line cannot have both debit and credit amounts");
        }

        var totalDebit = activeLines.Sum(l => l.Debit);
        var totalCredit = activeLines.Sum(l => l.Credit);

        if (Math.Abs(totalDebit - totalCredit) > 0.005m)
            throw new Exception("Journal entry debits and credits must balance");
    }

    private static bool IsManualJournalEntry(JournalEntry journalEntry)
        => journalEntry.SourceTypeId == (int)SourceType.Journal
            && journalEntry.JournalEntryKindId == JournalEntryKind.Manual;

    private static bool IsUserEditableJournalEntry(JournalEntry journalEntry)
        => journalEntry.SourceTypeId == (int)SourceType.Journal
            && journalEntry.JournalEntryKindId is JournalEntryKind.Manual
                or JournalEntryKind.OpeningBalanceSheet
                or JournalEntryKind.RetainedEarnings;

    private static bool IsDeletableManualJournalEntry(JournalEntry journalEntry)
        => IsManualJournalEntry(journalEntry)
            && journalEntry.PostingStatusId is PostingStatus.Open or PostingStatus.SoftClosed;
}
